Shri Mataji: Jeśli wprowadziliście ich w temat pujy, to nie mam nic przeciwko, ale jeśli nigdy nie zostali zaznajomieni z tematem pujy, wtedy musicie być ostrożni. Dla ich dobra. Przykro Mi, że musiałam to powiedzieć, ponieważ wczoraj obciążenie było tak duże, że nie mogłam nawet mówić, a podnoszenie Kundalini było jak podnoszenie stu osłów na waszą głowę, to było zbyt wiele. I wszyscy ci na wpół upieczeni ludzie są jak pies ogrodnika, sam nie weźmie i drugiemu nie da. Będą się kłócić przed programem, zanim spotkanie się rozpocznie, a szczególnie zanim rozpocznie się puja. Ponieważ nie chcą, żebyście cieszyli się owocami pujy; zepsują waszą uwagę, będą się kłócić, dyskutować. [Na stronie: "Proszę usiąść".] O co można się spierać w Sahaja Yodze? Musicie to przyjąć, to jest celem, musicie czerpać, nie ma się o co kłócić.

Sednem jest, jak dużo zdołacie przyjąć. A najgorsze jest to, że nie chcą wzrastać i chcą was ściągnąć w dół. Więc dopóki nie zostaną wcześniej wprowadzeni w temat pujy - nie zaczną rozumieć wartości pujy, jak oni mają to zrozumieć? Widzicie, to ich przerasta. Więc przynajmniej nie powinniście zmuszać ich do udziału w jakiejkolwiek pujy, a także w seminariach. Ponieważ seminaria są przeznaczone dla ludzi, którzy intensywnie angażują się w Sahaj Yogę, aby pogłębić swoją świadomość. Nie dla osób, które są na wpół upieczone, którzy są tzw. "niewiernymi Tomaszami", wszyscy tego rodzaju ludzie są całkowicie bezużyteczni, nigdy nie powinniście zabierać ich na seminarium. Czy jest to wasz syn, ojciec, brat, matka, siostra, mąż, żona, nic z tego. Nie powinniście zabierać nikogo, kto nie jest w pełni przebudzonym Sahaja yoginem, ponieważ seminaria są nastawione na bardzo intensywne wypracowywanie.

I co odkrywam, ci ludzie zawsze będą się ze Mną kłócić, są pierwsi do zadawania pytań, mają coś do powiedzenia, to właśnie oni. Sahaja yogini utrzymują ciszę i chłoną to. Nie mam nic przeciwko temu, ale na seminarium musicie otrzymać więcej. A oni nie przyjmują, ich uwaga jest na zewnątrz, przeszkadzają wszystkim. Tak więc seminaria są w pewnym sensie nawet ważniejsze niż ashram, ponieważ w ashramie nie ma innego problemu oprócz walki z nimi [tymi problemami]. Ale tutaj, w nowym miejscu, do którego przybywamy, musimy walczyć z całym otoczeniem, samo to miejsce musi zostać oczyszczone, wszystko musi być zrobione. Ponadto macie tych okropnych ludzi wokół was, których musicie przekonać. Jeśli nie są przekonani, po co tu są? Dlaczego wydali tyle pieniędzy, żeby tu przybyć? Bądźcie pewni, że przybyli tutaj, aby nam przeszkodzić.

Przykro Mi, że musieliśmy to tak zrobić. Chciałabym, żeby ludzie byli prości, bezpośredni, nieskomplikowani i wrażliwi, ale oni tacy nie są. Nie są wrażliwymi ludźmi. Oni nie rozumieją, co robią. Przez to, bez wątpienia niszczą samych siebie, ale oni także niszczą część was, a także sprawiają Mi kłopot. Wczoraj było zbyt ciężko, wiecie o tym. Taki głęboki temat i było zbyt ciężko dla Mnie. Jeśli powiedzą, że źle się czują, w porządku, powiedzcie im: "Przykro nam, nie możemy ci pomóc". Widzicie, są pewne momenty, w których musimy mieć swobodę decydowania o tym, kto może wejść. To jest moment, kiedy zamykamy drzwi.

I nie ma w tym nic złego, ponieważ musicie ich bardziej wyposażyć, aby ich wydostać. Od zawsze była to nasza praktyka, nie ma w tym nic nowego, wszyscy wiedzą, nie tylko w Anglii czy w innym miejscu, nawet w Indiach: od samego początku nigdy nie pozwalaliśmy przychodzić na puję nikomu, kto nie był Zrealizowaną Duszą i kto nie był Sahaja yoginem.

Gdziekolwiek tego próbowaliśmy, okazywało się, że było to bardzo trudne. Mam nadzieję, że nie siedzi tutaj nikt z umysłem pełnym wątpliwości. Jeśli jest ktoś taki, powinien z całą szczerością opuścić salę i zostawić nas samych, przez wzgląd na wzrost. Każdy, kto ma wątpliwości, powinien być łaskawy i wielkoduszny w tej sprawie. Praca, którą wykonujemy jest bardzo poważna. Nie chodzi o zarabianie pieniędzy, czy o zakładanie firmy lub budowanie domu czy coś takiego. To nie jest nawet tylko tworzenie światów, to znacznie więcej, to bardzo poważna praca, którą wykonujemy i nie chcemy, żeby ktokolwiek zakłócał naszą uwagę. Nawet niektórzy Sahaja yogini, których znam, są niedojrzali.

Muszą być korygowani, w porządku, nie szkodzi, ale oni zostali wystawieni na puje, więc to im zrobi dobrze, a nie źle. Ale niektórzy ludzie są po prostu anty-boscy i mają alergię na Boga. Na każdą pracę, którą zaczynacie robić w imieniu Boga, są tak uczuleni, że reagują. To są alergie na Boga wyrażające się na różne zabawne sposoby. Teraz wszyscy Sahaja yogini muszą to wiedzieć, wtedy nie posuną się z nimi za daleko. Odkryjecie - powinniście ją odkryć u każdego, kto taką alergię ma. Powinniście powiedzieć: "Przepraszamy, ale nie mamy z tobą nic wspólnego". To jest dla ludzi, którzy powinni mieć zaszczyt czczenia.

Dzisiejszy dzień jest szczególnie uprzywilejowany, ponieważ nie planowaliśmy tego w ten sposób, ale to się udało, po prostu zadziałało. Wczoraj zrobiliśmy dużo oczyszczania.

Teraz, dzisiaj powiedzcie swojemu umysłowi, aby odpoczął przez chwilę. Powiedzcie swoim myślom, aby zatrzymały się na chwilę i odbierajcie, przyjmujcie więcej. Wszyscy skorzystali na tym przyjmowaniu, ale musicie przyjąć więcej. Macie w sobie możliwość przyjmowania. Puja, jak mówiłam wam wiele razy, nie jest dla mojej korzyści, w żadnym wypadku, uwierzcie Mi. Mam się o wiele lepiej bez niej. Bo kiedy pełne entuzjazmu Bóstwa zaczynają emitować wibracje na waszą prośbę - ponieważ jesteście świętymi, One stają się po prostu zbyt entuzjastyczne. One siedzą, "wszędobylskie", siedzą cicho czekając na szansę, aby emitować wibracje, aby was uratować. One nie mają czasu do stracenia. Emitują tak wiele, że się gubią - "Po prostu poczekajcie, poczekajcie". Chcą was uratować, chcą wam dawać, są tak żądne tego, w swoim zatroskaniu - lubię patrzeć, w jaki sposób są zatroskane.

Ale wy tego nie absorbujecie, a Ja mam wtedy problem. Gdy raz dały, nie chcą zabrać tego z powrotem. Kiedy wy tego nie zaabsorbujecie, [wibracje] muszą pójść do atmosfery, muszą gdzieś się podziać. One są kierowane do was, a wy ich nie przyjmujecie, więc gdzie one idą? Trzymają się Moich tkanek, to Mnie boli. Więc absorbowanie powinno być większe. Absorbowanie powinno być większe. A tuż przed pują powinniście powiedzieć: "Matko, uczyń mnie takim, żebym absorbował więcej Boskości". Po prostu módlcie się szczerze od serca, po prostu powiedzcie to.

