cokoli. Abyste svou mysl tedy udrželi čistou a prostou, používejte element
vody, jak je to jen možné. Pomáhá proti obojímu, egu i superegu. Používejte
element vody. A to je Lakšmí. Lakšmí je ta, která vám dává Mahálakšmí tattvu.
