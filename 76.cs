kterému se říká Rakhi Púrnima, uvážete na ruku týchž bratrů a sester rakhi. V
Indii jsme to také tak udělali a fungovalo to velmi dobře. Tam jsou ale tyto
vztahy velmi silné. Jednou Dhumal uvázal rakhi Raulbáj. A když teď i Já něco
řeknu Raulbáj, nelíbí se mu to. Dosáhlo to až takové míry. Je to tak milé. A i
Dhumalova žena si Raulbáj váží jako starší sestry. Je to velmi sladké. Takovým
způsobem vzniklo mnoho vztahů. A v následujícím roce se Raulbáj stala sestrou
jednoho z našich správců, který už jím není. A ona ten vztah udržovala dál a
oni také, představte si. Celé je to o tom, že tento vztah je tak výjimečný, tak
blízký a tak čistý. A takový vztah by si měl každý rozvinout s ženou, s
jakoukoli ženou nebo přítelem, kterého chcete mít. Měli byste si pamatovat, že
nemůžete překročit určitou hranici, musíte mít marjádu. Jinak se stanou bratry
a sestrami a pak se vezmou. To není možné. Musíme mít tedy hranice. To je velmi
důležité. V Sahadža józe musíme mít marjády a hranice. A jednou z nich je, že
jakmile jednou někoho nazvete bratrem nebo sestrou, jedná se o naprosto čistý
vztah. Když půjdeme trochu dál, tak vztah, jak jsem vám řekla už dřív, s
mladšími bratry vašeho manžela je právě takový, jsou jako vaši mladší bratři.
Všichni manželovi mladší bratři, nebo sahadžajogíni, kteří jsou mladší než váš
manžel. A naopak - všechny ženy, lze říci, manželky všech mužů, starší ženy,
starší než vaše ženy, jsou jako sestry, starší sestry. Tento vztah je tak
sladký a dává vám jistou míru pochopení, jaký odstup si s kým udržovat, s kým
být svobodný a u koho se chovat zdrženlivě. A to je třeba udržovat tak
nádherným způsobem, aby se tato čistota nezkazila. Držíte se čistých standardů.
Čistota je výživa Sahadža jógy, výživa vašeho růstu - čistota. Čistota je pro
nás tou nejdůležitější věcí, čistota ve všem. Pokud jste čistí, nebudete dělat
nic, co je falešné, co je, nebo spíš, co není čisté. Nejprve budete tedy sami
čistí a přijmete čistotu, budete si vážit čistoty, budete posilováni čistotou.
Nebudete spolupracovat nebo se snažit dělat kompromisy s žádnou nečistotou.
Proto je čistota jméno vaší Matky. Víte, že Mé jméno je čistota. A tato čistota
je taková, že prostě očišťuje, prostě čistí. Čistota by měla být taková, že
čistí, jinak tato čistota nemá význam. Například mýdlo čistí. Ale vy nečistíte
mýdlo. Čistíte mýdlo? Možná nějací blázniví lidé. Znám lidi, kteří myjí dokonce
