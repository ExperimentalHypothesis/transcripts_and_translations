Božství. To je velmi neurčité, protože Božství... lidé neporozumí, jak to
souvisí s vodou. Pojďme na to tedy odjinud. Co dalšího? J: Zklidnit emoce. J:
Ochladit emoce. Stanete se zklidňujícím člověkem. Velmi dobře. Velmi dobře.
Stanete se ochlazující osobností. Máte ochlazující efekt. Co dál? J: Nosit
druhé. Můžeme druhé nést. Ano, velmi laskavě. A některé také utopit. Ponořit je
do lásky, do oceánu. Ano. Můžete je ale také postrčit dvěma rychlými skoky. To
je také možné. Voda má další ohromnou kvalitu - že proniká až do nejmenších
částeček. Jde ke kořenům. Je nasávána do kořenů. Díky síle Mahálakšmí tedy
jdete ke kořenům onoho člověka. Prostě víte, odkud ten problém pochází, vrhnete
se na něj. Je to ve skutečnosti vědomí, je to inteligence, která nasaje pravdu,
jejíž část se osvítí. A vy pochopíte, kde je problém, jdete prostě ke kořenům a
nasajete to, dokážete se do toho snadno vnořit. Pronikáte. Chápete, když sedíte
v blízkosti nějakého člověka, řeknete Mi: „Matko, je napadený tady vzadu,“ a
ten člověk o ničem neví. Pronikli jste. Vaše osobnost pronikla do toho člověka.
On nic neví. Pronikli jste do toho člověka a vnímáte kořeny jeho problému. Voda
vtéká do té bytosti. Božská voda teče do té bytosti a díky této božské vodě
cítíte blokádu a pak můžete říct, je napadeno tohle, tamto... Takže pronikání.
A dále je to lesk vody. Vaše kůže se začne lesknout, začne zářit. A to poslední
je odraz. Člověk, který vás uvidí, uvidí svůj vlastní odraz. A dostane strach.
Někdy se to u Mě stává. Nechci pro nikoho nic špatného, ale někdy ze Mě lidé
mají zbytečně strach, protože uvidí svůj odraz. Takže když vás potkají, uvidí
svůj odraz a mohou být velmi agresivní, protože vidí svůj odraz. Mohou být na
vás velmi tvrdí, protože vidí svůj odraz. A je-li voda průzračně čistá, uvidíte
hloubku toho člověka. Když jste čistý sahadžajogín, vnímáte tu hloubku jasně a
skrze ni vidíte, jak Duch září. Toto jsou kvality vody uvnitř nás. Zejména Já
ve svém těle, řekla bych, té vody shromažďuji víc, abych chránila čakry.
Kdybych neměla v těle vodu, mohla bych mít problém. Takže funguje jako tlumič.
Také uklidňuje buňky vaší pokožky; lze říci, že zklidňuje epitel vaší sliznice.
Víte, když chybí vlhkost, trpíte na dutiny. Zklidňuje všechno. A existují další
síly vody, které známe. Když vyjde z vašich očí, vyjadřuje váš soucit, vaši
lásku, vaše emoce. Je to jediný element ze všech, který vyjadřuje emoce. Mám na
mysli sílu vody. Voda má také další kvalitu, fyzici ji znají jako hydrostatiku,
má tlak. Vytváří tlak. A člověk, který je sahadžajogín, bude určitě vytvářet
tlak na druhé. Nebude vůči druhým agresivní, ale bude tu tlak. A když druhý ten
tlak přijme, bude z toho mít výhodu. Když ho ale nepřijme, bude se cítit
utlačený a trochu ovládaný. Vaši sousedé, členové rodiny cítí zpočátku tlak,
protože tělo získá něco jako váhu a tak se z vody vytváří guru tattva. A pokud
je tam i sůl, pak váží guru princip ještě víc. Takže o vodě bychom si dnes měli
uvědomit, že je velice posvátná, používáme ji také při našich púdžách, a
zejména toto symbolizuje Kundaliní. Kundaliní je totiž džbán na vodu neboli
nositel - Vodnář. A toto je věk Vodnáře. Jak důležitá je tedy voda. A voda je
nositelem Boží lásky. Je nejlepším nositelem. A toto je věk Vodnáře, což
znamená, že Kundaliní je stvořena z Božské vody. Takže celá dnešní oslava
Lakšmí nás má přivést k pochopení, že Ona se v nás stala Mahálakšmí. Díky tomu
máme hodně sil, jichž si nejsme vědomi. Chci říci, že si nejsme vědomi, že jsme
realizované duše. Někteří z nás to neví. A že máme v sobě tyto síly, že můžeme
pronikat do druhých. Můžeme svou osobnost promítat do druhých. Můžeme zjistit,
co s nimi není v pořádku. Můžeme se dovědět něco o sobě. Můžeme jít do sebe.
Naše pronikání je takové, jako když se pohybuje voda. Stále teče. A to je velmi
důležité, že bychom si měli být vědomi svých sil, kterých jsme dosáhli. Začneme
si pak vážit sami sebe a pak se přidá Rádžalakšmí a pak se přidá Gruhalakšmí a
přidá se všechno ostatní, když si uvědomíme, co jsme získali - jakou sebeúctu,
jakou důstojnost. Dává nám tedy důstojnost, Mahálakšmí nám dává důstojnost. A
tak začneme rozumět hodnotě lidské důstojnosti. Měli bychom si také uvědomovat,
co znamená lidská důstojnost, že nemůžeme urážet lidskou důstojnost. Jako lidé,
kteří druhým pořád nadávají. To je proti lidské důstojnosti. Nebo se stále
snaží prosazovat a dělají druhým ze života peklo. To je proti lidské
důstojnosti. Musíte zdobit život druhého, musíte ho učinit krásným, musíte ho
učinit radostným a šťastným, učinit každého šťastným, ne stále do někoho rýt.
Když sekýrujete, to není Gruha Lakšmí; to není Gruha Lakšmí. Nesekýrujte druhé:
„Udělej to a ono, postav se, co děláš, postav se tady, pojď sem!“ To všechno je
nesmysl. Proč někoho k něčemu nutit? To nejdůležitější je lidská důstojnost.
Musíte ctít lidskou důstojnost. A je to možné, protože teď máte sílu
Mahálakšmí. Ti, kteří nemají důstojnost, ji od vás mohou získat, protože vy
jste důstojní. Máte-li smysl pro důstojnost, nikdy nebudete urážet důstojnost
druhých. Kvůli maličkostem, malichernostem urážíme druhé. „Proč jsi dal mýdlo
sem? Proč jsi udělal tohle, proč tamto?“ Na druhé straně druzí možná nemají
důstojnost a dělají věci jako zvířata. Oba případy jsou špatně. Musíme mít
úctu, s tou dosáhneme nejlepších výsledků vůbec. Nejlepší je, máte-li úctu a
lásku, pak vzrůstá důvěra a důvěru je třeba udržovat. Tímto způsobem lidská
důstojnost podporuje růst sahadžajogína. Musíme si vážit každého sahadžajogína,
protože jsme všichni svatí; važme si jeden druhého, s naprostou úctou, vždyť
jsme všichni svatí. Nevím, zda si uvědomujete, co vám teď říkám. Každý z vás je
schopen dát realizaci celému světu, kdokoli z vás. A pokud si to uvědomíme,
musíme si vážit sebe i druhých. Neměli bychom mluvit o druhých lehkomyslně,
dělat si z nich legraci. Obzvlášť v případě vaší Matky, k Ní se nemůžete chovat
neuctivě. Tohle byste měli vědět, na Západě je to důležité. Je velmi důležité,
abyste o tom věděli. Nedávno jsem někoho opravovala a Hugh mu pak řekl: „Máš
veliké štěstí, že tě Matka opravuje; máš velké štěstí, že tě Matka opravuje a
něco ti říká.“ Protože jste naivní, víte, proto začnete mluvit neuctivě.
Především musíte ctít důstojnost vaší Matky, To je velmi důležité. A to vám dá
tu největší důstojnost. Říkám vám to, protože pokud to nebudete dělat, nebudete
zdárně růst. Takže tento bod je velmi důležitý. Jako když jde třeba někdo v
Indii do chrámu. Nebo jdou... Například dnes přišla ona nebo někdo... bylo by
lepší, kdyby si oblékly krásné šaty. Víte, vždyť jdete do chrámu! Tehdy se
ozdobí různými šperky, vším možným. To je den, kdy musí projevit veškerou
důstojnost. Takže by nikdo neměl přijít s rozcuchanými vlasy nebo něčím
podobným. Není to správně, protože všechny tyto věci jsou důležité, jak
projevujete svou úctu. Jakým způsobem se musíte oblékat, jak být
reprezentativní, protože tyto věci... Někteří řeknou: „Ó, já žiju Duchem,
nemusíme být upraveni, protože žijeme Duchem.“ Duch je ten nejupravenější. Dává
do pořádku všechno kolem. Dává vše do pořádku. Vše dává na správné místo, do
souladu, do jednoty, vzniká z toho krásná melodie. Jak může být chaotický
člověk nazýván duchovním člověkem? Nemůže! Je-li někdo chaotický, i v jeho domě
a jeho věcech bude chaos, musíte vědět, že je v nich nějaký bhút, zcela určitě.
Ale nesmíte se stát opakem, být natolik úzkostliví, až všechny otrávíte. To je
druhý extrém. Neměli byste dělat ani jedno. A já na tom trvám, jelikož je to
velmi důležité pro egoisticky orientované lidi, protože z těch se může stát
cokoli. Jednou jsem jela po King’s Road a byla jsem překvapena, jak se lidé
oblékají. Myslí si, že jak se oblečou, takoví jsou. To znamená, že musí být
opicemi, když se oblékají tímto způsobem. Pokud v to věříte, musíte být spořádaní
lidé, musí být ve vás slušnost. Dnes jsme si řekli o tom, jak se z Lakšmí stala
Mahálakšmí. Neplatí ale, že pokud do něčeho vyrosteme, to ostatní zmizí. Není
pravda, jak si někteří myslí, že když se stanou duchovní bytostí, Lakšmí tattva
zmizí, tak to není. Musíte být důstojnou osobností. Slušně oblečeni v souladu s
vaším postavením a způsobem života. Nemělo by v tom být žádné, jak tomu říkáte,
pokrytectví, žádná přetvářka. Žádné výstřední, nesmyslné chování, jako třeba,
když si někdo myslí, že primitivním životem a podobně udělá na druhé dojem. Tím
nikoho nezaujmete. To je postoj žebráka. Pokud budete jako žebráci, jen do vás
vnikne nějaký žebrácký bhút. Nemají se z nás stát žebráci, ani nějací frajírci,
ale sahadžajogíni, důstojní lidé. Máme sebeúctu a žijeme důstojně. Proto musíme
být čistí, transparentní, upřímní, musíme být upřímní sami k sobě, zcela
upřímní. Voda je k sobě velmi upřímná: všechno, co do ní vložíme, je vidět.
Přidáme-li do ní barvu, ukáže to. Dáme-li do ní bahno, ukáže to. Musíte být
velmi upřímní, upřímní sami k sobě, a tímto způsobem by z vás měla vyrůst
osobnost, která je příjemná, krásná, pronikající, to ano, avšak velmi příjemně
osvěcující. Protože Mahálakšmí tattva sama o sobě je osvícením v našem vědomí.
Takový člověk nemůže být idiot. Pokud je idiot, není to sahadžajogín, určitě
ne. Idiotický člověk nemůže být sahadžajogín. Hlupák, roztržitý, to vše musí
odejít. Pokud máte být sahadžajogíni, nemůžete říkat: „Óóó, já vím, já vím.“
Tyto nesmysly by měly zmizet, neměli bychom je mít. „Lakšany“ nebo jak nazýváte
ty příznaky, můžeme říct... Jak nazýváte lakšany? Způsob, jak rozpoznáte
sahadžajogína? To bude jako Laskšmí putra - syn Lakšmí, jako král. To
neznamená, že má vše pro sebe, ne, tak to není. Král dává. Štědře obdarovává. Je
proslulý svou štědrostí. Není proslulý tím, kolik si nahrabe, že ne? Je králem,
to znamená, že dává. Měl by tedy být „cool“, pohodovou osobností. Neměl by být
chladný jak led. Měl by být „cool“, ne vznětlivý. Ti, kteří jsou vznětliví...
viděla jsem zde lidi, kteří se hned uchylují k násilí. Násilí je pro
sahadžajogíny naprosto nepřijatelné. Bez debat! Ani když vás někdo udeří, vy
byste neměli oplácet. Máme své způsoby, jak je bít. Neměli byste ale oplácet.
Neměli byste se jim stavět na odpor. Ale pokud jste sahadžajogíni, nesmíte na
nikoho používat násilí. Ani slovní. Někteří jsou takoví, že provokují slovem.
Pochopte, že tohle všechno musíte napravit. Je to důležité, protože vy zítra
budete ukazovat světu, co znamená Sahadža jóga, ne Já. Musíme se tedy zasvětit
své očistě, oslavování sebe samých, protože voda oslavuje, dává vám slávu.
Vypadáte všichni tak krásně, září vám tváře, vidím to na vás, Kanti Rupena
Samsthita. Řekl: „Ya Devi Sarva Butheshu Kanti Rupena,“ zář na tváři, světlo. A
to je to, co zde vidím na vás všech. Už to máte, ale musí to být i uvnitř vás.
Pak člověk, který je realizovanou duší, vám nemusí přísahat, že jí je. Lidé
zkrátka poznají, že je realizovanou duší. Nemusíte to nikomu říkat, lidé to
poznají z toho, jaký je. Dnes jsme probrali mnoho bodů. Doufám, že si je opět
poslechnete, protože přednášky Šrí Mátadží jdou jedním uchem dovnitř a druhým
ven. Díky Bohu mám způsob, jak je zachovat. Doufám, že si tyto kazety vezmete a
budete je poslouchat, jednu po druhé, každý den. A také konat, ne plánovat na
papíře nebo v hlavě, musí to vejít do srdce. Takto všichni dospějete v nádherné
děti. Jen počítám, Sir C.P. bude opět zvolený na čtyři roky a pak budu muset
odejít. Budou to sotva tři roky, a až uplynou, už nebudu moci jezdit do Velké
Británie tak často. Odpovědnost přejde hlavně na vás. Doufám, že spolu ještě
nějaká Divali oslavíme. Nechť vám Bůh žehná! J: Matko, mohu se na něco zeptat?
Šrí Mátadží: Ano, ano, prosím. J: Často se říká, ať nereagujeme na násilí, což
je v pořádku, když někdo páchá násilí na nás. Ale co dělat, když někdo páchá
násilí na někom jiném? Co dělat pak? ŠM: Jakže? J: Pokud někdo páchá násilí na
někom jiném, měli bychom jen přihlížet a dovolit to, nebo co bychom měli dělat?
Šrí Mátadží: Dejte jim bandhan. Máme svoje metody k boji s těmito věcmi. Dejte
jim bandhan, Dejte jim bandhan, takhle, zvedněte jim Kundaliní. J: Děkuji.
Nejde mi o sebe, u mě je to v pořádku, ale jde-li o dítě... ŠM: Sledujte tu
hru. J: Ano. ŠM: Jasné? J: Ano, děkuji. ŠM: Nechte je bojovat. Co můžete dělat?
Nechte je, ať se spolu srovnají sami, chápete? J: Co si myslíte o vojenské
službě ve vyspělých zemích, Matko? ŠM: Cože? Co říkal? J: Ptá se na vojenskou
službu. ŠM: Je to hloupá věc, kterou musíte dělat, protože jste se narodili v
této hloupé zemi. To bylo vaše hloupé rozhodnutí narodit se tam. Pak musíte
dělat tyto hloupé věci podle vaší země. V Indii také musíme dělat některé
hlouposti. Ne takové, ale jiné. Pokud jste se narodili v hloupé zemi, jako je
Francie, je lepší to udělat. Další, kde to mají, je Švýcarsko. Nikdo zatím
neútočil na Švýcarsko, víte, přesto mají tuto vojenskou službu, protože
okrádají lidi o peníze, a mají ty banky, takže musí ukázat, že mají armádu na
obranu peněz. J: Máme alternativu: můžeme napsat vládě a... ŠM: Ne, ne, lepší je
dát bandhan, psát je zbytečné. To je pořád ještě něco mentálního. Šubitujte to.
Vaše vláda se teď mění. J: Ano. ŠM: Hodně se to změnilo. J: No... ŠM: Mnohem
lepší, teď. J: Možná ano. ŠM: Mnohem rozumnější, že ano? J: Ano. ŠM: Takže -
modlete se za ostatní vlády. Všechno se vypracovává. Všechny ty hloupé věci tu
dosud jsou, co dělat? Musíte teď jít na vojenský výcvik? J:Ano, možná, Matko
.... [jiný jogín navrhuje civilní službu]. ŠM: Navrhovala bych jednu věc. Víte,
když... pokud budete muset jít na vojnu, nevadí. Tam nikoho nezabijete, je to
jen legrace. Je to jako komedie. Takže nic špatného. Meditujte, dělejte
všechno. Není to nic vážného, je to vše jen fraška. Udělejte si dovolenou.
Berete to tak vážně, vždyť nejdete do války! Žádná válka už nebude. Nechte je
hrát celou tu frašku. Nějaké další otázky? Všechno se hodně zklidnilo. Síla
vody vám stoupá vzadu po zádech. No a teď bychom měli provést púdžu. Dnešní
púdžou si musíte rozvinout Mahálakšmí tantru /tattvu?/. Všechny tyto síly se
musí prokázat praxí, proto provádíme tuto púdžu. Rozumíte? Už jsem vám
vysvětlila, co znamená Mahálakšmí princip. A všechny tyto síly se ve vás musí
probudit. Stále natáčíte?
