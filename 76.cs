
Dnes jsem vám řekla o důležitosti Lakšmí principu v nás a
třech procesech, jimiž procházíme. Prvním je Gruha Lakšmí. Slaví se většinou
třináctý den nové fáze měsíce a říká se, že hospodyně musí dostat nějaký dárek.
A nejlepší věc, kterou může dostat, je nějaké nádobí. Můžete jí tedy dát
nádobí. Vlastně je to oboustranně výhodné, protože pokud dostane nádobí, musí
pro vás uvařit. Je to velmi milý způsob, jak jí říct, aby pro nás něco uvařila.
Následující čtrnáctý den je velmi důležitý, protože to je den Bohyně jako síly.
Síly, protože to byl Kartikéja, kdo zabil démona Narakasuru. A to byl jeden z
nejhorších démonů, nebylo možné ho zabít. Šiva a Párvatí se tedy spojili
speciálně za tímto účelem, aby vytvořili tohoto mocného Šaktiputru, to znamená
syna Šakti, známého jako Kartikéja. A on se narodil právě proto, aby zabil toho
hrozného ďábla, nazývaného Narakasura. A On ho zabil. To bylo čtrnáctý den.
Tento den je něco jako Halloween. Protože toho dne se otevřely brány pekla a
všichni ďáblové byli uvrženi do pekla. A proto bychom v tento den měli ráno
odpočívat tak dlouho, jak jen to jde. Ať se tam hezky dostanou všichni ďáblové,
protože někteří mohou ještě být venku. Je lepší nechat je odejít. V oblasti, ve
které jste, jejich existenci moc nevnímáte. Kdybyste ale byli v oblasti
podvědomí nebo kolektivního podvědomí, dokonce byste některé duchy viděli a
také byste cítili jejich existenci, viděli černé skvrny a podobně. Tento den je
tedy velmi nebezpečný. Ale protože Narakasura byl zabit, lidé oslavovali. Tato
oslava probíhá následující den, kterému se říká Divali. V tento den se také
uctívá Lakšmí, protože se má za to, že v tento den se Lakšmí zrodila na Zemi z
moře, jak jsem vám řekla minule. Poté přichází Nový rok, protože na severu
Indie lidé tou dobou dokončují sklizeň a zjistili, že tento den je velmi dobrý
k oslavě Nového roku. A lze říci, že v Indii je to spíš společenská záležitost,
nemusíme se jí držet, pokud to tak necítíme. Můžete mít svůj vlastní Nový rok.
Na jihu, v Maháráštře, mají totiž Nový rok jindy, jak jsem vám řekla.
Šaliváhana tam ve své dynastii založil jiný kalendář a slaví se podle něho.
Takže musíme vědět, že nic z toho není pevně dané. Nic není pevně dané.
Neochota ke změnám pochází z neznalosti. Všechno je to pro vás, pro vaši radost
a štěstí. Celý systém svátků a oslav je pro vaše potěšení, a ne pro potěšení
daného času nebo dne. Je pro vás, proto by měl být přizpůsobitelný podle vašich
potřeb a vašeho chápání. A také podle vašeho národního způsobu života. Neměli
byste být připoutaní k žádnému konkrétnímu druhu kultury, konkrétnímu druhu
představ. Je to všechno pro vás. Všechny tyto svátky jsou pro vás, ne pro Bohy.
Oni tyto svátky nemají. Tyto svátky jsou zde jen pro vás. A když je slavíte, je
to jen pro vaši radost a vaše štěstí. A měli byste je slavit tak, aby vám daly
co nejvíc potěšení. A měly by se slavit všechny svátky na světě. Čím více
slavíte, tím lépe. Proč vynechat příležitost, chápete? Jako sahadžajogíni
můžete tedy slavit. Nemůžete se... Posuňte se více na tuto stranu... Jako
sahadžajogíni už teď nepatříte k žádné kastě nebo skupině, patříte k
univerzální kastě. Jakmile začnete být univerzální, můžete slavit všechny
svátky všech náboženství. A mít z nich to nejlepší. Celý rok můžete slavit
všechny svátky, poznat je, čím více o nich víte, tím lépe. A jezte všechny
druhy sladkostí, jakékoli máte ve zvyku, jakékoli, v jakýchkoli oblastech,
zemích a kulturách. Takže tímto způsobem musíme více slavit. Poslední den je
velmi důležitý, je třeba říci, že je to velmi univerzální den. Nazývá se Bhaija
Dudža nebo také Vao Didžum v maráthštině, říkají mu různě. Je to den bratrů a
sester. Bratrsko-sesterský vztah musí být nesmírně silný. A je třeba zachovávat
čistotu takového vztahu. Není nezbytné, abyste se narodili jako bratři a
sestry, není to nutné. Pokud jste však realizované duše, narodili jste se jako
bratři a sestry. Ve společenství Sahadža jógy je tedy třeba zachovávat tuto
čistotu. Je velmi důležité, abychom se snažili vybudovat si tento postoj, že
bratři a sestry jsou něco velmi čistého. Jejich vztahy jsou velmi čisté. A
neměli byste to v žádném případě pokoušet. Platí to do takové míry, že i když
někoho jen nazvete svým bratrem, stane se vaším bratrem. A čím více si
rozvinete tento vztah, tím lepší bude vaše levá Višudhi. Protože levá Višudhi
je vztah s Bohem jako bratrem nebo s Bohyní jako sestrou. Šrí Krišnova sestra,
jak víte, se narodila Nandovi a Šrí Krišna sám se narodil ve vězení. Takže
došlo k výměně. Museli Ji položit na Jeho místo. A když se pak Kamsa, Krišnův
strýc, tuto dívku pokusil zabít, vznesla se k nebi a coby blesk řekla: „Ten,
který tě zabije, stále žije v Gókulu, dosud je tam.“ Takže blesk, který vidíme,
představuje tuto sílu, které říkáme Višnumája, síla sestry. Pokud bude váš
vztah k ženám jako sestrám správný a silný, vaše levá Višudhi bude velmi dobrá.
Má mnoho dalších vlastností. Například člověk, který má velmi dobrou levou
Višudhi, je velice sebejistý. A nejen to - cokoli řekne, se splní. I když něco
řekne omylem, stane se to. Levá Višudhi je velmi mocné centrum. Pokud takový
člověk mluví, aby něco kritizoval, něco omezil nebo proti něčemu vystoupil,
jeho řeč má sílu rozdrtit všechno, co se mu nelíbí. Jako například Kristus,
Jeho levá Višudhi se projevuje Jeho vztahem k Máří Magdaléně. Když vystoupil a
řekl: „Ti, kdo nespáchali žádný hřích, ji mohou kamenovat“, projevil svou sílu.
Jejich vlastní hřích ty lidi zcela rozdrtil, nemohli nic dělat, protože tu byl
hřích. A ten hřích pocházel z levé Višudhi. Nebyla v nich žádná čistota. A když
chybí čistota a člověk, který je tak čistý, řekne takovou věc, všichni tito
lidé ztratí veškerou sílu. Nemohli ani kamenovat. Později se Jeho sestra
narodila jako Draupadí, a to velmi blízko místa, kde jsme byli, Vani. Víte,
když jsme viděli... Gavine, vzpomínáš si, když jsme jeli do Nágpuru? Narodila
se blízko Vani, a díky tomu je tam veliký chrám. Jmenovala se Draupadí a vy
víte, jak se Šrí Krišna snažil chránit Její cudnost. Slyšela jsem, že se lidé
také snaží říkat něco proti Máří Magdaléně a Kristovi a všechno možné. Je to
známka úpadku našeho rozumu a naší morálky, že nedokážeme v žádném vztahu vidět
čistotu. V žádném vztahu nedokážeme vidět čistotu. Jako sahadžajogíni si to
tedy musíme více rozvinout. Místo toho zjišťuji, že se lidé, zejména na Západě,
snaží spekulovat, koho za koho provdat. Že takové vztahy vznikají, je špatně.
Není třeba domlouvat svatby a hrát tyto romantické hry a podobně. Pokud má vzniknout
manželství, stane se to. Nekazte ale atmosféru. Někteří lidé jsou jako
dohazovači, snaží se domlouvat manželství a jsou velmi pyšní, že zařizují
sňatky, toho s tou a té s tím. Není to žádoucí. Nemělo by se to dělat. Na
neustálém domlouvání svateb není nic ctnostného. Když přijde čas na svatbu,
dojde k ní. V době Mohameda Sáhiba vyvstala velká otázka, protože bylo velmi
málo mužů, ale mnoho žen všech věkových skupin. A nevěděli, co dělat, jak ty
dívky zachránit, protože mnoho mužů zemřelo ve válce. Ptali se tedy: „Co máme
dělat v této katastrofální situaci, kdy je zde tolik žen, ale velmi málo mužů?“
Řekl: „Dobrá, můžete si vzít čtyři nebo pět žen jakéhokoli věku.“ Protože žít
podle dharmy znamená žít ve společensky schváleném svazku, ne bez manželství. Řekl
tedy: „Dobrá, můžete se oženit až pětkrát, ale musíte se oženit, nemůžete žít
bez sňatku.“ A proto trval na tom, aby byli všichni sezdaní a nežili ve vztahu
bez manželství, což byl hřích. Jenže dnes si muslimové myslí, že mají právo
vzít si pět žen, i když je mužů víc a žen méně. To je špatně. Musíte najít
řešení. A v té době dokonce i vztah mezi sourozenci musel být vymezen, protože
měli velký problém. Řekl tedy: „Takže, děti stejných rodičů se vzít nemohou,
ale bratranci a sestřenice ano.“ V té době. Ale doba se změnila, je jiná doba.
