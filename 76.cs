jste pokorní. Pokora je něco jiného než ustrašenost. To je něco úplně jiného,
nemá to nic společného s pokorou. Být pokorný je tedy něco jiného než bát se.
Pro sahadžajogíny je velmi snadné zjistit vibračním vědomím, co je strach a co
je pokora. Neboť pokora vám dá vibrace, ale strach napadne celou levou stranu.
