Wspomnienia z wykładu Pasquale na temat Pujy - seminarium Easter 2015

Główne części Pujy:

Ganesha Puja
Wykonują dzieci. Najlepiej jeżeli masują Stopy podczas polewania ich wodą. Dzięki temu mają bezpośredni kontakt z Devi. Dodatkowo można dodać do wody olejku zapachowego co powoduje, że dzieci chłoną też zapach ze Stóp Matki.

Ofiarowanie Amrutu

Koleność: ghee, miód, jogurt, mleko, cukier z szafranem. 

Gauri Puja
Osoby dotykające stóp Devi powinny być całkowicie czyste, dlatego tę część powinny wykonywać niezamężne dziewczęta. Jednakże w praktyce, wielokrotnie podczas tej części młode dziewczęta z przejęcia dokonywały wielu błędów, dlatego z czasem Stopy Matki dekorowały również kobiety zamężne.

Devi Puja
Wykonywana głównie przez zamężne kobiety, które ofiarowują Devi wszelkie ornamenty.

Niektóre pytania

Pytanie: Czy wąchamy kwiaty, które chcemy ofiarować Matce?
Pasquale: To tak jakbyśmy chcieli spróbować kanapki, którą zaraz komuś podarujemy. 

Pasquale kiedyś zapytał: Matko czemu malujemy Twoje Stopy?
Shri Mataji: Ponieważ one wykonują całą pracę.

Pytanie dotyczyło różnych rytuałów podczas Pujy pochodzących z praktyk bramińskich.
Pasquale odpowiedział, że Matka generalnie starała się wszystko utrzymywać w prostej formie. Jednakże będąc w Indiach mimo wszystko akceptowała rozmaite rytuały ponieważ jest Boginią i może wszystko. My musimy jednak sprawdzać czy dana czynność ma wibracje. Niektóre z takich lokalnych rytuałów mają wibracje inne nie.

Pytanie: Kto powinien ofiarowywać amrut?
Pasquale odpowiedział, iż każdy dorosły bez względu na płeć. Mogą też być Yuva shakti.

Pytanie: Czy przygotowujemy 4 różne pojemniki z kum kum? Jedno do bindi, drugie do kokosów, trzecie do owoców, a czwarte dla Shri Mataji?
Pasquale odpowiedział, że przygotowujemy tylko dwa. Jedno do dekoracji Stóp Shri Mataji i drugie do wszystkich pozostałych rzeczy.
Pytanie: Czy bindi na zdjęciu Shri Mataji robimy przed czy podczas pujy? (nie pamiętam dokładnie, ale chyba to było w takiej formie).
Pasquale odpowiedział, że dla nich (w Anglii) bardziej praktycznym jest zrobienie bindi przed pują po umyciu zdjęcia, gdyż potem podczas pujy nie muszą wykonywać niewygodnych akrobacji by sięgnąć do zdjęcia. Dodał, że dobrze wiemy jak to było wcześniej kiedy Shri Mataji przychodziła na Puję mając już bindi i podczas pujy jedynie je poprawiała. 

Rahul Bhasale zrobił komentarz odnośnie ofiarowania elementów Amrutu oraz kwiatów, jako esencji kreacji Bogini, która podczas Pujy do niej wraca w formie ofiary.