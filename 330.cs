
Dnes oslavujeme otevření Sahasráry, které se odehrálo k
tomuto datu. Musím říci, že to byla velmi významná událost pro celé lidstvo.
Bylo to dosažení něčeho úžasného, co jsem si dříve ani neuvědomovala. Teď
vidím, že bez seberealizace by nebylo možné hovořit k lidem. Po té události
jsem si říkala: „Jak bych o tom s lidmi mohla mluvit, vždyť by tomu nikdo
nerozuměl.“ Z Mé strany by byla velká chyba zmiňovat se o Sahasráře. Protože o
Sahasráře se ani v žádných Písmech... Něco málo bylo popsáno, ale byl to velice
neurčitý popis, řekla bych, podle něhož se lidé nedokázali dovtípit, že nad
Sahasrárou je další svět, do kterého je třeba se dostat, do říše reality. To,
co jsem tehdy viděla kolem sebe - cítila jsem, že je to všechno temnota a že
dokud nebude mnoho světel, lidé si nikdy neuvědomí, jak je důležité mít světlo.
Je to typická chyba lidí, že pokaždé, když někdo něčeho dosáhne, postaví si tu
osobnost na piedestal. Například Krista - dobrá, byl to Kristus, což my nejsme.
Mohamed Sáhib byl Mohamed Sáhib, my nejsme Mohamed Sáhib. Ráma byl Ráma, Krišna
byl Krišna, tímhle my nejsme. Jak by někdo mohl čekat, že lidské bytosti budou
jako Oni? Oni jsou totiž naprosto odlišní. Božské osobnosti jsou na hony
vzdálené lidským osobnostem. Bylo to jen o zbožňování, řekla bych, o víře, ale
když říkali, že je zkouší následovat, nedokázali to. Nešlo je následovat, mám
totiž za to, že lidský pohyb probíhá paralelně s Božskou úrovní. A tyto úrovně
se nesetkají, dokud jim nedáte realizaci. Lidská úroveň běží v jiné rovině a
musí se dostat na vyšší rovinu, aby lidé pochopili, že to, co dosud znají, není
realita. Neuváděla bych, co je k tomu zapotřebí. Z vlastní zkušenosti totiž
vím, že do Sahadža jógy přišli nejrůznější lidé. Lidé považovaní za velice zlé,
kruté, další velmi nemravní a podobně, jiní podvodníci... Až jste si Mi
stěžovali: „Matko, oni jsou příšerní!“ Ale Já jsem viděla, že každý člověk je
stvořen podle stejné přirozenosti, co se týče Ducha. Navenek mohou působit
rozdílně, mohou mluvit rozdílně, jejich způsoby mohou být různé, jejich záliby
mohou být jiné. Ale uvnitř jsou všichni nádherní. Přicházela jsem na to u
jednoho po druhém. A neumíte si představit, kolik Mi to dávalo radosti, a nejen
radosti, ale i povzbuzení, podpory a trpělivosti. A teď vidíte, jak se Sahadža jóga
rozrostla. A to nejlepší, co se stalo, co jsem si uvědomila a z čeho mám velkou
radost, je, že sahadžajogíni cítí za Sahadža jógu velkou zodpovědnost. A cítí,
že toto osvícení musí šířit všude. Šířit Sahadža jógu je teď jejich niterná
touha. Nejprve si přáli stát se Duchem. Ještě před tím tu byli i lidé, kteří v
podstatě Ducha nehledali. Ale i z těch se stali sahadžajogíni. A někteří z vás
Mě překvapili, protože bych nikdy nečekala, že se dostanete tak daleko -
najednou přijde zpráva, že někdo jel tam a tam, udělal tohle, udělal tamto.
Když jste se tedy dostali do říše reality, nevím, který princip jste přijali
nejdřív, to se řídí vaším vlastním chápáním. Jedno vám ale došlo, že jste
rozhodně spojeni s Božským. Někteří měli úžasné prožitky, někteří jen slabé,
ale většina z nich, jak jsem viděla, začala věřit v sebe sama, začali rozumět
sami sobě a získali sebejistotu. Protože existuje velmi mnoho mýtů o Bohu, o
náboženství, o guruech, takže když spatřili světlo Ducha, které jim dalo smysl
pro realitu, povedlo se jim ustálit mysl, ustálit sebe sama. Tohle všechno se
stalo, musím říci, během velmi krátké doby. Neřekla jsem, že Mi to trvalo hodně
dlouho nebo že jsem musela projít mnoha různými zkušenostmi. Protože pro Mě
jsou všechny takové zkušenosti bezvýznamné. Když je loď hotová k vyplutí,
dokáže čelit všemožným přílivům, nejrůznějším žralokům, čemukoli, bez
sebemenších potíží. Protože je schopna plout po moři. Takový pocit jsem sice
neměla, ale cítila jsem, že když přecházíte z lidské úrovně do úrovně duchovní,
Božské, když přecházíte, tak něco z toho předešlého stále přetrvává, něco z
toho ještě zůstává. Musí se to vyčistit, musí se to, řekněme, prosvětlit nebo
zcela osvítit. Na Sahadža józe je však skutečně skvělé to, že jste to všechno
udělali vy sami. Je hezké, že říkáte: „Matko, Ty jsi udělala to a ono.“ Snad tu
část s osvícením. Řekněme však, že jsou zde lampy, ale pokud nejsou udržované a
nikdo se o ně nestará, pokud do nich nikdo nedolévá olej lásky, jak by mohly
stále svítit? To je něco, co musím uznat, že jste nějak dokázali pochopit, jak
důležité bylo, abyste se stali dobrými sahadžajogíny. Možná díky introspekci,
možná vám to někdo poradil, možná jste to pochopili sami, ať to bylo jakkoli,
všichni jste se snažili být dobrými sahadžajogíny. Samozřejmě jsou i lidé,
kteří se na Mě někdy hrozně zlobí: „Proč jsi nevyléčila mou sestru?“ Nebo:
„Proč jsi nevyléčila mého otce, jestliže jsi Bůh?“ a tak podobně. Ale Mně to
vůbec nevadí. Jsou velmi frustrovaní, velmi rozrušení, proto tak mluví. Takoví
se najdou jeden nebo dva, tu a tam, ale tady jsou tisíce překrásných lidí!
Úplné osvobození této Země, planety Země, zcela jistě nastane, jestliže vy
všichni převezmete zodpovědnost. Se zodpovědností jsou určité potíže, o kterých
bychom měli vědět. Když cítíte zodpovědnost, musíte si uvědomovat, že vy to
neřídíte. To zaprvé. Zadruhé si musíte uvědomovat i to, že tu jsou mnohé další
síly, mnoho andělů a ganů, kteří jsou s vámi. Nejste sami. Myslet si tedy, že
něco děláte, z vás možná udělá egoisty. Tehdy je nejlepší říct si: „Dobrá, já
nedělám nic, to Božské to vypracovává.“ Ne že by Bůh chtěl, abyste Ho za to
oceňovali, ale když si to budete říkat, ten balón ega se nenaduje a vy budete
pokorní. S touto pokorou se rozhodně ozdraví ten velkolepý strom duchovnosti uvnitř
vás. Zcela jistě vás to učiní velmi vyspělou osobností, o tom není pochyb.
Především si však musíme říct: „Já nedělám vůbec nic.“ Je tak důležité poznat,
kdy máte duchovní pravomoc konat. Není to zkouškové období, opravdu. Hodně lidí
si myslí: „Matka nás zkouší.“ Já nikoho nezkouším. Sami se zkoušíte. Neříkám:
„Nedělej tohle, nedělej tamto.“ Vy sami se zkoušíte a teď si musíte uvědomit,
že se nacházíte v překrásné zahradě reality, kam se jich dostalo jen pár. A
když vstoupíte do té zahrady, co uvidíte? Že vše je plné krásy, každý člověk má
v sobě něco milého. Protože všichni ostatní sahadžajogíni mají stejný prožitek
této reality, kterou nelze popsat slovy, začnete však cítit tu jednotu, tu
vzájemnou blízkost, to přátelství, bez sebemenšího očekávání. Začínáte cítit:
„Ó, to je sahadžajogín.“ Jako byste se stali kompletně integrovaným organismem,
nebo, řekla bych, organizací, která je živoucí. Pak nikdo necítí, že byste byli
jakkoli odděleni - žádný sahadžajogín, ať už má vysokou pozici, nebo toho v životě
mnoho nedosáhl. Tato integrace, tato jednota, tento pocit sounáležitosti s
druhými vás tolik vyzdvihuje, jste tak jedineční, tak významní a tak mocní.
Nejste sami a říkáte si: „Já nedělám nic.“ Stanete se tedy tak pokornými, že se
oprostíte od všech těch falešných idejí o kastovním uvědomění, o náboženském
uvědomění, od všech těch různých uvědomění, ani nevím, co všechno lidé mají.
Moc se nevyznám ve všech těch uvědoměních, která člověka postihují, ale viděla
jsem, jak k tomu dochází. A když se ta pokora stane vaší přirozeností... Ne, že
máte pokoru proto, že je třeba něco udělat. Vy jste pokorní! Když lidé
například v něčem podnikají, budou extrémně pokorní a milí, ale jinak umí být
velmi nepříjemní. Tady se pak ale stáváte zcela pokornými, protože ve vaší
osobnosti už není žádná dualita. To ale neznamená, že se stanete slabými.
Pokorní mohou být jen lidé, kteří jsou silní, protože nepotřebují nijak
reagovat, nepotřebují se ničím chránit. Jejich pokora je navíc udržuje pod
velkou ochranou. Začnete si tedy myslet... spíš cítit, myslet je trochu
zavádějící, cítit, že jste nepatrnou částečkou prachu, a zároveň cítíte, že
jste celou oblohou. To záleží na vás, kde jste, co děláte, co vidíte, čemu jste
svědkem. Někdy máte pocit, že všichni ti velcí Arnoldové jsou úplně k ničemu.
Cítíte, že byste se jimi neměli zabývat, že jsou velice přízemní. Ale vedle
jiných si připadáte maličcí. Rozvinete si až tak flexibilní povahu, a ta vás
zjemní, budete čím dál jemnější. Protože dokážete proniknout do čehokoli, do
jakéhokoli tématu, do každé osobnosti, každého porozumění, do každé knihy, do
čehokoli, do každé činnosti velmi subtilním způsobem a okamžitě poznáte, co je
třeba udělat. Protože budete tak subtilní. A pak, když se stanete opravdu
velkými, pak vás začne napadat, tedy spíš budete cítit, co můžete dělat -
pomoci mnoha lidem, vykonat tuhle ohromnou práci, udělat tamto. Když se s vámi
tohle stane, nikdo si nebude myslet, že máte ego. Ani byste se ale sami neměli
cítit maličcí. Jste teď jen a jen Duchem a Duch je velmi subtilní. Je to velmi
subtilní světlo, které však dokáže proniknout do čehokoli, které se dokáže
kamkoli rozšířit, dokáže setrvávat kdekoli, ale může se z čehokoli vytratit.
Tuto subtilní osobnost, kterou jste, což je váš Duch, vy ani nepoužíváte, to on
neustále používá vás. Používá vás, aniž byste o tom věděli. Zadruhé byste si
neměli určovat, co budete dělat. „Co budu dělat? Když jsem se rozhodl pro
tohle, musím dělat tohle!“ Ať jste se rozhodli pro cokoli, v pořádku, ale
jestliže se to neuskuteční, není proč si dělat starosti, není proč se
rozrušovat, není důvod být kvůli tomu zbytečně nervózní. Dám vám příklad.
Jednou jsem jela do Ameriky a víte, jak to chodí, všichni sahadžajogíni
znervózní: „Matka teď musí jet do Ameriky,“ tak se začnou nervovat. Hlídají
čas, teď je tolik hodin, teď tolik... Já se jen smála. Řekla jsem: „To Já musím
jet do Ameriky, proč se nervujete vy?“ A náhle dcerka jedněch sahadžajogínů
upadla a zlomila si ruku a nemohla hýbat prsty. Zrovna jsem byla na odchodu.
Ale jakmile ji ke Mně přinesli, řekla jsem: „Teď na letiště nepojedu.“ Byli tak
rozrušení: „Tak, a Ona teď nejede na letiště!“ Jako by měla spadnout nebesa, či
co. Řekla jsem: „Nechte Mě teď podívat se na to dítě,“ a vyléčila jsem ji.
„Dobrá, tak teď pojeďme na letiště.“ Začali: „Matko, to letadlo už určitě
odletělo.“ „Nevadí, pojeďme.“ Dorazili jsme na letiště a to letadlo, kterým
jsem měla letět, tam stále bylo, protože mělo nějakou poruchu. A bylo tam další
letadlo, do Washingtonu, a Já jsem popravdě chtěla letět právě do Washingtonu,
ne do New Yorku. A tak jsem spokojeně letěla do Washingtonu. Musíte prostě
vědět, že vy nekonáte. Všechno je zařízeno a naplánováno a pěkně to na vás
čeká. Když se pro něco rozhodnete, dobrá. Pokud bylo vaše rozhodnutí správné,
jestliže to máte udělat, bude to fungovat. Ale jestli to nedopadlo, tak to tak
ani nemělo být. A to je ten rozdíl mezi realizovaným člověkem a člověkem bez
realizace. Člověk, který je realizovaný, se nestará o to, co by chtěl, co by
rád, co ho těší. On se nestará. Je spokojen s čímkoli. Jak to vidím tady, kde
spíte pod tak příšernou střechou, myslím tím... Není tu žádná pořádná
organizace, nic, nevím, jak to tu zvládáte, ani nevím, co s tím. Snažíme se
získat pozemek, ale ten majitel je takový chamtivec. Nevadí Mi jeho hamižnost,
ale on nám ten pozemek ne a ne dát. No dobrá, co naděláme? A vy si to užíváte.
Vám to nevadí. Běžně by totiž lidé začali: „Víte, chci takový a takový pokoj,
požaduji takový typ domu a podobně.“ Vy můžete žít v jakýchkoli podmínkách,
protože se už nestaráte o pohodlí svého těla. Už sledujete jen svůj duchovní
komfort. Viděla jsem lidi, sahadžajogíny, tak krásné, i děti, které když
sledují film, ve kterém se objeví něco nekalého, hned všichni zavřou oči:
„Tohle vidět nechceme!“ Nikdo jim to nemusí říkat, víte, protože si v sobě
vybudovali tu jemnější osobnost, která nesnese nesmysly. Možná nic neřeknou,
nebudou vyvádět, jen se tomu vyhnou a odejdou z takového místa. Automaticky. Tu
novou osobnost je tedy možné popsat takto. Jak to vidíme zcela jasně v přírodě.
V přírodě si můžeme všimnout, že když roste strom, musí se rozvětvit. Ale když
to není možné, tak to neudělá. Když bude moci, rozvětví se. A také se všemožně
snaží vystavit všechny své listy slunečním paprskům. Neřekla bych ale, že
stromy mají dostatek inteligence, aby si to naplánovaly. Je tu však něco, co je
tak nádherně řídí. Přitom to není jen jeden strom, ale tisíce a tisíce a
tisíce. Ten, kdo to všechno dělá, je ten, s kým jste ve spojení. Stromy nejsou.
Ony jen spolupracují s přírodou. Nemohou však ovládat, řídit tuto Boží sílu,
ani jí rozumět. Nemohou. Zvířata také ne. Ani lidé nemohou. Ale vy můžete,
protože jste překročili toto lidské omezení a teď jste prostě něco jako
významní občané velkolepé říše Božského. Proto vám tato Boží síla pomáhá, vše
pro vás vypracovává. Ona se vás ujala, vede vás, řídí vás, od vás je tedy
zapotřebí, abyste zůstávali otevření tomuto vedení. Zároveň ale musíte jednat,
protože to vedení přijde také z nitra; díky němu budete vědět: „Tohle je to, co
mám dělat.“ Řekněme však, že se přesto trápíte kvůli něčemu, čeho jste
nedosáhli. Že nevíte, proč jste dosud nedostali to, o co jste usilovali. Zkuste
pochopit, že to je pro nás to nejlepší. To ovšem nikdy neznamená, že se máte
vzdát, že byste si neměli přát. Viděla jsem, že přání sahadžajogínů jsou mnohem
mocnější než Moje. Je to proto, že Já po ničem netoužím. Nemám žádná přání jako
taková. Zkuste na to přijít, odhalit, jestli mám nějaká přání. Vím, že žádná
nemám, vůbec žádná. Žádné Mé přání není vroucné. Může vás napadnout, že to je
velmi lehkomyslná nátura. Ale s vámi lidmi je to úplně jiné. Vy lidé jste
stvořeni úplně jinak. Protože vy jste něčeho dosáhli. Já nedosáhla ničeho. Jsem
tím, čím jsem. Protože vy jste něčeho dosáhli, víte, jací jste byli předtím a
jací jste teď. Zřejmě díky tomu, že jste něčeho dosáhli, jste získali
požehnání, že cokoli si přejete, se vypracovává. Protože jste pracovali tak
tvrdě, máte odměnu, mimořádné požehnání, ať si přejete cokoli. Tolikrát jsem
vám už říkala: „Nepřejte si nic, co není správné, protože cokoli si přejete, to
se splní.“ A bude to působit. Zkuste tedy pochopit, že už nejste obyčejní lidé.
Překročili jste to omezení. A teď už nejste ani v Sahasráře, ale nad ní.
Sahasrára je limbická oblast. A tato limbická oblast je uzavřena, jako lotos. A
když do ní vstoupí vaše Kundaliní, pak se ten lotos takto otevírá. A to je to,
co se píše v Bibli: „Zjevím se vám jako plamenné jazyky.“ To jsou Boží slova.
Tyto okvětní plátky nejsou tedy nic jiného než nervy, které jsou osvícené.
Právě jako tyto. Vypadají jako plameny. Tyto plameny však v sobě nemají žádné
horko. V sanskrtu se tomu říká „tápa-hína“ - bez horkosti. Nesmírně
ochlazující, konejšivé, překrásně planoucí. Když se tedy tento lotos Sahasráry
otevře, pak vaše Kundaliní projde skrze vaši fontanelu a vy získáte realizaci.
To je to, co se s vámi stalo. Teď jde jen o to, udržet tento lotos otevřený. A
v tom selháváme, někdy, protože v nás stále setrvávají některé lidské pocity.
Především je to každopádně překryto egem a podmíněnostmi, tedy superegem, dvěma
balóny. Tyto dva balóny se stále znovu snaží Sahasráru uzavřít. Musíte být
tedy... musíte se mít na pozoru: „Co uzavírá mou Sahasráru?“ Především je to
ego, které útočí. Protože chcete zaútočit na své ego, a to můžete zas jen skrze
ego. Je to jediný nástroj. Proto máme stav nirvičára, bezmyšlenkové vědomí, do
kterého se musíme uchýlit. Je však třeba dělat jednoduchou věc, velmi
praktickou, zkoušejte zjišťovat, proč jste egoističtí, na koho se zlobíte, proč
se zlobíte, proč máte tendenci někomu dominovat nebo někoho ovládat, případně
někoho kontrolovat. Proč? Mysl vám neodpoví. Když dokážete dělat tuto
jednoduchou věc - kdykoli vás něco nahněvá nebo způsobí, že jste namyšlení,
zkoušejte pochopit, že vám se nic nevyrovná. Nic není větší než vaše láska. Nic
není větší než vaše milosrdenství, protože v tom všem proudí světlo Boží. Když
někoho nesnášíte, na někoho se zlobíte, jen si připomeňte, že ho máte milovat.
A jak toho člověka milovat? Na to není žádné měřítko, ale láska sama vám to ukáže,
podle toho, nakolik budete spokojení, nakolik budete šťastní. Dám vám příklad.
Jednou jsem dostala opravdu hodně provokativní dopis od někoho z Polska, že
její matka dostala rakovinu nebo něco podobného, ve kterém Mě opakovaně
napadala: „Jestliže jsi Bůh, musíš vyléčit mou matku, jestliže jsi Bůh, udělej
to a ono.“ Skutečně vydařený. Řekla jsem: „No dobrá.“ Zaměřila jsem na tu paní
pozornost, možná se matce ulevilo. Ale místo toho, abych se rozzlobila, řekla
jsem si: „Je tak frustrovaná, tak rozrušená, plná obav. I když na Mě útočí,
jestli jsem Bůh, je to v pořádku, o nic nejde. Jestli Mi říká Bůh, nebo ne, nic
to nemění. Jsem tím, kým jsem.“ Takže místo toho, abych se na ni rozzlobila,
cítila jsem k ní ohromný soucit. Je to lidská bytost a její matka je tak
nemocná, její jediný smysl života, pak dokážu pochopit, proč je tak rozrušená.
Když se tedy jen pokusíte... což vám půjde opravdu velmi snadno, není to těžké,
protože jste realizované duše, mluvím k světcům. Když prostě jen zaměříte
pozornost na člověka láskyplně, pak se stane to, že budete mít pochopení, že
tomu člověku porozumíte. Uvědomíte si... jako byste se s tím člověkem
ztotožnili, uvědomíte si: „Kdybych byla na jejím místě, jako lidská bytost, co
bych cítila? Tuhle beznaděj, tuhle zlost. Kdybych byla jí, kdybych byla stejný
typ člověka, se stejnými problémy, co bych dělala?“ A to ten problém vyřeší.
Takže to ztotožnění se... Máte takové síly, jak jsem vám říkala, můžete
pronikat. To ztotožnění se s druhým je tedy velmi důležité. Dokážete to. Jste totiž
realizované duše, proto to umíte. Jen si představte sami sebe v situaci, v jaké
je ten druhý. A pak poznáte, že máte síly lásky. Myslím, že ztotožnění se je
jediný způsob, jak se dá porozumět lásce. Tohle ego okamžitě splaskne, jak jen
na toho člověka zaměříte láskyplnou pozornost. Během života uděláte tolik
takových zkušeností, že užasnete sami nad sebou: „Jak to všechno zvládám?“ Díky
tomu, že jste realizované duše. Také, jak jsem už řekla, když se s druhým
ztotožníte, předáte toho člověka do Boží péče. Jen tím vnímáním jednoty. Někdy
máme skutečně naprosto klamné představy o sobě i o druhých. I v maličkostech,
máme na ně své vlastní názory. Řekněme, že někteří lidé vyžadují určitou
životní úroveň: „To máme rádi. Tohle nemám rád! Tohle mám rád, tohle rád
nemám.“ Světec ale ne. Světec tohle neřekne nikdy. Když tohle říkáte, nesprávně
se ztotožňujete s badhou jiného člověka. Například, řekněme, někdo udělal
takhle krásnou květinovou výzdobu. Místo abyste to ocenili, užili si to,
přijdete a řeknete: „Mně se to nelíbí, mohlo to být lepší.“ Jen aby ukázali...
Nevím, proč lidé takhle mluví. Když se však ztotožníte s tím, kdo to vytvořil,
užijete si to stejně jako ten, kdo to tvořil. Ale protože myslíte jen sami na
sebe, máte sebestředné smýšlení. Proto vás napadá, že tohle neudělali
dostatečně dobře, že to či ono mohli udělat lépe... Kdo jste, abyste kohokoli
soudili? Tato kritická povaha vzniká obrovským nepochopením sebe samého. Kdo
jsme? Proč bychom měli soudit kohokoli jiného? Je to snad naše povolání? Proč
tedy plýtvat energií na souzení druhých? Měli bychom se zaměřit na to, co je v
každém dobré, protože ve sféře reality je vše dobré a v pořádku, vše je
prvotřídní. Když se snažíte vidět, co je na ostatních špatné, pak nejste v
realitě. Můžete pozorovat, být svědkem, ale nerozčilujete se kvůli ničemu, ani
neříkáte nic, co by druhé znepokojilo. Díváte se na všechnu tu krásu. Chci
říci, že radost toho člověka, který to sem dal, všech lidí, kteří to tvořili,
všechny tyto krásné věci, z toho přímo čiší. Ale dokud se neztotožníte s těmi,
kteří to vytvořili, mohli byste říkat, že to či ono by mohlo být lepší. Můžete
se dostat do stejné pozice. Řekněme, že něco uděláte a někdo spustí: „Ó, tohle
si měl udělat tak a tak.“ Plýtvání energií pro nic za nic! Naše energie je
duchovní energie. Znovu říkám, že budete jak nebesa, ti nejvyšší. Jste příliš
vysoko, než abyste se starali o takové věci. Je to plýtvání energií na velmi
triviální věci, na zbytečnosti kdy jen srážíte sami sebe. Když se však tato
duchovní energie používá... Protože vy ji můžete používat, vy jste ti jediní,
kteří s ní mohou nakládat, ne stromy, ani květiny, pak dokážete vytvořit oceány
radosti. Pak se dostáváme k sahadž. Co sahadž dalo vám, to je v pořádku. Ale co
jste dali Sahadža józe vy? Co jsem dal Sahadža józe já? Vytvořil jsem pro
ostatní oceány radosti? Jakou laskavost jsem prokázal? Jaké soucítění jsem
projevil? Kolik lásky jsem projevil? Dobrá. Cokoli nám dala Sahadža jóga, to
jsme vzali za samozřejmé. Jak přijdou do Sahadža jógy, mají pocit, že je to
kontrakt, už podepsaný. A když se něco nevypracuje, pak se diví: „Měli jsme
přece s Bohem smlouvu!“ Říkají: „Nebyli jsme plně doceněni, za vše, co jsme
vložili, za to, kolik jsme toho udělali.“ Ale co jste udělali v této říši
reality? Těšili jste se realitou? Cítili jste radost z té opravdové Boží lásky?
Jste v ní rozpuštění? Nebo stále jen pozorujete z břehu? Vy můžete tvořit! Máte
dostatečné schopnosti vytvářet obrovskou radost, nesmírné štěstí a mír.
Podívejte se, v jakém stavu je dnešní svět - naprostý chaos, zmatek, korupce
všeho druhu a neustávající destrukce. Svět tolik zošklivěl! Dokážete ho
zkrášlit? Dokážete. Protože umíte lidi transformovat. Ale pokud sami nejste
krásní sahadžajogíni, jak byste mohli? Je tedy třeba mít pozornost na sobě samých,
když se připravujeme na to, že musíme přivést ostatní do této říše reality. To
je tedy jedna stránka nás samých, že musíme poznat svou vlastní subtilnost, a
ta druhá je, že se stále ztotožňujete s věcmi, s nimiž bychom se ztotožňovat
neměli. Můžeme na to prostě přijít. Ztotožněni s určitými nápady, ztotožněni s
jakýmsi uvědoměním... Všechny tyhle věci, když budu opravdu upřímná, z vás
dělají hlupáky. Všichni hlupáci, víte, kteří mají podmíněnosti, jsou
skutečně... ani nevím, co na ně říct, protože jsou jako vlak bez strojvůdce.
Bůhví, kdy vykolejí. Protože takoví lidé, kteří jsou podmínění, znám... Jistě
jste také viděli ty, kteří chodí do kostela. Pěkně se vyfiknou, jdou do
kostela, posadí se tam, zazpívají chvalozpěvy, jdou domů a zas se hádají a perou.
Ale když vidí kostel, pokloní se. Nic si z toho kostela ale neodnesou. Jsou
všichni stejní, jako, víte, jistě jste viděli, že židé mají zeď nářků, před
kterou se postaví a dělají všechno možné, a k čemu jim to je? K ničemu. Stejně
tak muslimové. Jsou tak podmínění! Jejich podmíněnosti prostě ani nelze popsat,
tak jsou hloupí. A tyhle podmíněnosti stále pokračují a dělají z nás hlupáky.
Poznala jsem tolik takových lidí! Jednou jsem zažila, že byl u nás jeden
příbuzný, který měl ve zvyku vstávat ve čtyři ráno a zpívat něco jako „Ráma
rakša“. Celý dům spí, děti spí, ale on křičí z plna hrdla. Ptala jsem se: „Co
se to děje? V tuto dobu nejspíš ještě spí i Ráma.“ Ale on bude vyřvávat. A
další věcí byly jeho koupele. Každý věděl, že se koupe, protože pořád zpíval
další a další verše. A jednou se stalo, že jsem byla ve vedlejší místnosti a
uslyšela jsem nějaké bouchání, rány, zděsila jsem se, co se děje. Napadlo Mě,
že má epileptický záchvat, či co. Běžela jsem tedy říct ostatním, že ten pán má
epileptický záchvat. Prý ale že ne, že nemá. Ptala jsem se: „Tak co to je, ten
zvuk?“ „Ne, ne, on jen dře čandan, víš, santálové dřevo.“ Pokaždé praštil
kamenem o zem s takovým bouchnutím, že jsem myslela, že má epilepsii. „Ať mu
řekneš cokoli, nebude Tě poslouchat, dělá to tak každý den.“ Co je to? Tomuhle
říkáme stupidita. Když vidíme ostatní, když vidíme lidské bytosti, ale i
realizované duše, které jsou v říši reality, občas akceptovat nereálné věci.
Kvůli podmíněnostem z dávné doby, protože vaši předci byli takoví, otcové i
předci předků, tak se vás to stále drží a říkáte tomu geny. Díky Sahadža józe
se ale i takové geny mění. Takže takové podmíněnosti, které stále ulpívají na
vašich tělech, ve vašich myslích, ve vašich emocích, je třeba uvidět a
vyčistit. Protože zabíjejí radost. Znemožňují plně prožít realitu. Jestli
chcete naplno zažít krásu reality, musíte se vysvléct ze všech těchto věcí,
které z vás skutečně dělají hlupáky od rána do večera. Tento typ lidí ostatní
hodně zneužívají, jak jsem už řekla, například podnikatelé. Přijdou s určitým
druhem módy a všichni to pak nosí. My ale nejsme lidé bez mozku. A nejenže máme
mozek, my máme osvícený mozek. Nenásledujeme věci, které jsou pouhou módou nebo
něčím podobným. My ne. Jak to děláme my? Držíme se své vlastní módy. To není o
egu. To je ztotožnění se se svým vlastním Duchem. Nedáte se na něco hloupého a
nesmyslného, když v sobě máte moudrost svého Ducha. V říši reality máme tedy
všech sedm čaker zcela osvícených, jednotných a vyživovaných. A z této pozice,
vnímáme sami sebe, jak jsem říkala, jako něco nekonečného, bez jakéhokoli
omezení. I když jen něco vyslovíme, tak pronikáme, opravdu. Když řekneme, že se
rozšiřujeme, je to tak. Stejně tak u smršťování se. Je to absolutně flexibilní
osobnost, osobnost, která vytváří mír, radost a pohodu. Jsem si jistá, že díky
otevření Sahasráry se jednou celý svět sjednotí a my porozumíme těm problémům,
které máme kvůli stupiditě. Jsem si jistá, že to sjednocení nastane. A až to
nastane, uvidíte, že život v Bohu bude zářit natolik jasně, že zastíní všechnu
tuhle temnotu a destruktivní síly. Stane se to. Protože když se podařilo
otevřít tolik Sahasrár, proč by nešlo otevřít i Sahasráru celého světa pro
pravdu a realitu? Stane se to. Dnešní den je pro nás tedy velmi významný. V
historii duchovnosti je to podle Mě velmi důležitý den, kdy bychom si měli
uvědomit, že jsme osvícení. Jsme světlem radosti, světlem milosrdenství,
světlem lásky. Tak jako tiché plameny Sahasráry, hrající všemi barvami v
překrásném tanci. Pak poznáte, co je to radost v realitě. Bůh vám žehnej!
