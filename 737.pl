
 Program Publiczny - Problem Ego,1979-1022 Caxton Hall, Londyn

Myślałam, żeby wam dzisiaj powiedzieć o problemie z ego, bo myślę, że każdy czuje, iż to jest największy problem dla wszystkich; z tym problemem można zabrnąć za daleko i nie wiedzieć, co z tym robić.  

A więc w pierwszej
części powiem wam, jak to ego się w nas powiększa i co w nas
powoduje; 

w drugiej części
powiem, jak to rozumieć, jak poradzić sobie z ego i jak sprawić by
powróciło ono na swoje miejsce. 

Jak widzicie tu na
wykresie, ten żółty obszar tu na górze to jest ego. Ten żółty
balon to jest ego. Widzicie, ono zaczyna się tu niżej, od
Swadisthanu. To jest kolor żółci w nas, i czakra Swadisthana,
która odpowiada za naszą kreatywność, jest bezpośrednio
połączona w tym miejscu ego. I kiedy ona zaczyna krążyć wokół
Voidu i docierać do różnych części Voidu, zbiera wszystkie
problemy Voidu. Void jest w nas tym zielonym okręgiem, gdzie na
poziomie fizycznym mamy w Voidzie macicę, mamy nerki - to są całe
trzewia: wszystkie jelita, okrężnicę wstępującą, poprzeczną,
zstępującą, wątrobę, głównie górną część wątroby,
następnie trzustkę i śledzionę. Zatem wszystkie problemy z tymi
organami są zbierane przez tę poruszającą się czakrę. Ona
wychodzi z czakry Nabhi i wciąż krąży dookoła zbierając
wszystkie problemy. Ona odżywia, daje tym organom siłę, życiową
siłę i również produkuje siłę niezbędną do naszych twórczych
działań. 

Ona zbiera również
komórki tłuszczowe Voidu i przetwarza je w komórki potrzebne na
użytek mózgu,      w szare komórki. Ta jedna czakra wykonuje całą
tę pracę. Ona manifestuje się na zewnątrz jako splot aorty. Na
poziomie fizycznym nazywamy ją splotem aorty, i ma ona sześć
pod-splotów, które opiekują się wszystkimi tymi organami. Ona
służy naszej akcji. Kiedy zaczynamy działać, ta czakra zaczyna
pracować. 

Dzięki pierwszej
mocy, która znajduje się po lewej stronie, my pragniemy. A dzięki
drugiej, zaczynamy działać i to nazywa się Kriya Shakti. Kiedy
pojawia się w nas akcja, wytwarza produkty uboczne, albo można
powiedzieć, tworzy problemy w tych organach, i wszystkie one muszą
zostać gdzieś przechowane, i zostają przechowane w mózgu w
postaci ego. Wszystkie problemy, które wynikają z tej twórczości
i działania tych organów, muszą zostać zrównoważone i ego
rozwija się jako przeciwwaga. 

Na przykład, musiałam
tu przyjść i spotkać się z wami wszystkimi. Musiałam wyjść z
domu, przebrać się, jakoś tu przyjechać, więc przyjechałam,
musiałam włożyć w to wysiłek. Musiałam zaplanować, o czym będę
z wami mówić - tego akurat zwykle nie robię, ale o tym mówię, bo
zwykle ludzie tak robią.            

Więc włożyłam w to
wysiłek, żeby tu przyjść. Jak mam to uzasadnić, cały ten
wysiłek? Jaką ma się satysfakcję z wykonywania tego wysiłku, tej
Kriya, tego działania? Wykonujemy jakieś działanie, po co to
robimy? Wszakże działanie oznacza, jakiekolwiek działanie oznacza
trochę zachodu, wytężenie sił, problemy, więc najlepiej jest
siedzieć w domu i nic takiego nie robić! Ale my tego nie robimy,
podejmujemy wyzwania, rzucamy się w wir działania. Wszystko to
robimy przy pomocy naszej Kriya Shakti, naszej prawostronnej akcji. 

Ponieważ wykonujemy
akcję, musimy mieć z tego satysfakcję. Jako reakcja na to rozwija
się ego. Jeśli nie mielibyśmy ego, nic byśmy nie robili - taki
jest fakt. Ale ego jest tym, które racjonalizuje nasze szaleństwo,
ten wyścig szczurów, w którym bierzemy udział. Jeśli nie
mielibyśmy ego, nie wchodzilibyśmy w ten nonsens. Im bardziej
staramy się racjonalizować nasze działania, tym bardziej rozwija
się ego, aby nas usatysfakcjonować. "W porządku, bardzo
dobrze, bardzo dobrze. Widzisz, jesteś teraz człowiekiem sukcesu.
Odniosłeś wielki sukces, masz to, masz tamto". Kiedy pompujemy
w ten sposób nasze ego, wkrada się dużo nieporozumień - albo
powinnam powiedzieć, że kiedy jesteśmy zadowoleni ze swojego ego -
wtedy tak naprawdę gubimy się. Identyfikujemy się ze swoim ego a
nie z samym sobą. 

A więc, jeśli coś
bardzo dobrze robicie: przypuśćmy, że napisaliście piękny wiersz
albo nie wiersz, ale powiedzmy, namalowaliście bardzo dobry obraz,
wtedy chcecie, żeby ludzie to docenili. Jeśli was nie docenią,
myślcie, że niczego nie dokonaliście, chociaż napisaliście
piękny wiersz albo stworzyliście obraz. Ale wciąż będziecie z
siebie bardzo niezadowoleni, dopóki ludzie was nie docenią. Muszą
udekorować was wieńcem, muszą powiedzieć: "O, jesteś
wspaniały, jesteś naprawdę unikalny, jesteś geniuszem".
Znacie wielu artystów, którzy tworzyli wielką sztukę i wpadli w
tego rodzaju pułapkę błędnej identyfikacji. Kiedy stworzyli tę
wielką sztukę, ta sztuka nie mogła im dać satysfakcji. Musieli
pójść w ego, a Pan Ego nie będzie zadowolony dopóki każdy nie
powie: "Tak, tak, jesteś bardzo dobry. To ty to zrobiłeś,
jesteś geniuszem, jesteś tym i tamtym". Tak właśnie rozwija
się w nas ego. 

Ale może zaistnieć
sytuacja, że nawet jeśli niczego nie dokonaliśmy, chcemy przypisać
sobie zasługi. Wtedy nazywamy egoistyczną, osobę która mówi: "Ja
to zrobię, ja to zrobiłem, ja, ja, ja, ja" - wtedy nazywamy go
egoistą. Ponieważ on daje głos swojemu ego, to dlatego nazywamy go
egoistą. Ale każdy        z nas ma Pana Ego siedzącego w swojej
głowie. Kiedy on zaczyna się nadymać, to im większe odnosicie
sukcesy, tym jest gorzej. Im więcej planujecie i myślicie, tym jest
gorzej. Widzicie, to jest tak jak z nadmuchiwaniem balonu, balon się
powiększa. Im więcej pompujecie, tym większy się staje. I nigdy
nie zdajecie sobie sprawy z tego, że nie jesteście tym ego. Potem
zaczynacie czuć się z tym bardzo dobrze, ponieważ każdy mówi:
"O, jaki to wspaniały człowiek". 

Całe społeczeństwo
rozwija się według tej zasady. Właśnie dzisiaj rozmawiałam o
tym, jak ludzie nadają wam "C.O.B" albo "V.I.P."
(Very Important Person = Bardzo Ważna Osoba), "M.A.D"

[magister – a słowo &quot;mad&quot; oznacza &quot;szalony&quot;]

 i
tym podobne, i my dźwigamy te sztandary. I widzicie, oczekujemy, że
każdy będzie szanował te sztandary, które nam przyznano, bo
wykonaliśmy jakąś pracę, ktoś to docenił, dał nam etykietkę,
przypieczętował, więc wierzymy, że to naprawdę musi być coś
wielkiego. Więc akceptujemy tę etykietkę i chcemy ją nosić. Więc
jest podwójnie utrwalone, że naprawdę zostajecie nagrodzeni za
pracę, którą wykonujecie. Z tego powodu ludzie stają się
uciążliwi. 

Przypuśćmy, że w
społeczeństwie niektórzy ludzie nie znajdują żadnego sensu w
swoim życiu. Wtedy pójdą na Oxford Street, wystają tam, nosząc
dziwaczne ubrania, zaczynają skakać i śpiewać coś bez sensu -
robić z siebie widowisko. Widzieliśmy ludzi, którzy farbują sobie
włosy na różne kolory, wkładają sobie ołówki do nosów, i
robią różnego rodzaju zwariowane rzeczy tylko po to, żeby
przyciągnąć uwagę innych. 

Ponieważ my chcemy
przyciągnąć uwagę innych, bo nasze ego nie jest jeszcze
usatysfakcjonowane. Ale to ego nigdy nie jest usatysfakcjonowane.
Domaga się więcej pożywienia, więcej pożywienia, i sprawia wam
to też przyjemność. Kiedy to ego jest w nas napompowane, nie
zdajemy sobie sprawy z tego, co robimy. Ludzie mogą was łatwo
ogłupić. 

O wiele łatwiej jest
ogłupić osobę kierowaną przez ego, niż osobę prostą. Widzicie,
człowieka prostodusznego nie jest łatwo ogłupić, bo on nie żyje
w iluzji. Ale ci, którzy żyją w iluzji ego, kiedy im powiesz, że
to jest najlepsza droga do piekła - oni pierwsi zarezerwują
miejsca. Ustawią się w kolejce i będą się o to dopraszać: "Och,
to dobrze, jeśli tak, to dobrze". To dlatego tak wielu guru
pojawiło się na świecie i ogłupiło was przez mówienie, że
dostaniecie te siły, tamte siły, to się stanie, tamto się stanie.

I oni wiedzą, jak
dogadzać waszemu ego. Na przykład, mogą powiedzieć, że to jest
światowa organizacja duchowa, a wy jesteście przywódcą światowej
organizacji - i wy w to wierzycie. 

Ego oznacza
sprzeniewierzenie. Wierzycie w nie, bo myślicie "w końcu muszę
być kimś, zrobiłem to, zrobiłem tamto". Ale to, co
faktycznie się dzieje na poziomie fizycznym, kiedy ten balon ego
pokrywa całą głowę - aura mózgu normalnie otacza serce,
normalnie, ale kiedy jest problem z ego, wtedy ego otacza serce. A
kiedy ego otacza serce, mózg jest odcięty od serca. Więc jesteście
osobą, która nie jest zintegrowana, albo możemy powiedzieć,
zdezintegrowaną. Wasze serce ciągnie w jedną stronę, wasz mózg w
drugą stronę, wasze ciało w trzecią stronę, a wasze emocje w
czwartą stronę. I czworo ludzi powozi czterema końmi, i oto jak
jesteście kompletnie rozdarci. To znaczy, trudno jest stać na dwóch
stołkach, ale kiedy macie cztery stołki na dwie nogi, to sama
egzystencja jest nie lada wyczynem. 

Więc zaczyna się ta
podróż w ego, przez którą zaczynacie zachowywać się i żyć na
tym świecie w bardzo nienaturalny sposób. I kiedy ktoś wam mówi:
"Nie, tylko nie twoje ego, nie to. To nie jesteś ty. Musisz być
sobą. Ego nie jest tobą. Nie zadowalaj się tym ego", wtedy
wam się to nie podoba. Ponieważ wy jesteście czymś o wiele
wspanialszym niż ego, o wiele większym niż ego, możecie dokonać
o wiele więcej, niż myślicie, że wasze ego mogłoby dokonać.
Powiedzcie, czego najwięcej możecie dokonać poprzez ego? Co
możecie zrobić poprzez swoje ego? Czego ludzie dokonali przy pomocy
ego? 

Niczego. Czegokolwiek
nie odkryli, nawet w nauce, przyszło to z obszaru niewiadomego, ze
skarbca samego Boga. Jeśli Bóg dał wam poznać trochę wiedzy
nieznanej, nie powinniście uważać się za kogoś szczególnego i
myśleć: "Och, to jest to, co my wynaleźliśmy". Nie, to
On łaskawie dał wam wiedzę, która do was przyszła, więc
przyjmijcie to z pokorą. A to ego jest bardzo, bardzo niebezpieczne
dla obudzenia Kundalini. Spójrzcie na wykres, wyobraźcie sobie, że
cała wasza głowa jest wypełniona tamtym żółtym balonem, a
niebieska część staje się bardzo mała. Wtedy przede wszystkim
maleje samo pragnienie poszukiwania. Widzicie, ruszacie na
poszukiwanie, tak jakbyście szli do kina. Nie ma połączenia tego
co myślicie, z waszym sercem. To nie przez serce poszukujecie, ale
właśnie przez rozum. Bo przeczytaliście trzy, cztery książki, bo
coś jest modne, bo każdy poszukuje, wygląda na to, że każdy
czegoś szuka, więc my też poszukujmy. To nie przychodzi z waszego
serca, bo możecie zobaczyć, że aktywność serca stała się
właściwie zerowa. 

Wy niczego nie
pragniecie, ale gonicie za tym, czego pragnie wasze ego. Wszystko to,
co nie satysfakcjonuje waszego ego, denerwuje was. Musicie ciągle
sobie powtarzać, że jesteście najszybszym koniem, najwspanialszą
osobą, najbardziej unikalną osobowością, tylko wtedy jesteście
usatysfakcjonowani. To tak bardzo was odcina od serca. To dlatego nie
możemy odnaleźć radości, ponieważ radość możemy poczuć tylko
poprzez swoje serce. Ale ten Pan Ego daje wam pewnego rodzaju
satysfakcję, która jest bardzo powierzchowna, tymczasowa i prowadzi
was donikąd. Tak, jak wam wcześniej mówiłam, przez nastawienie na
ego dostajecie tak wiele chorób, takich jak cukrzyca, atak serca,
można powiedzieć, że rak też może być jedną z tych chorób,
możecie też dostać artretyzmu. Dostajecie również paraliżu
przez nadmierne myślenie, właśnie to jest przyczyną paraliżu
oraz chorób nerek. 

Najgorzej jest z
wątrobą. Z powodu nadaktywności wątroba jest zaniedbana. W ogóle
nie przejmujemy się wątrobą. Jeśli powiecie dziecku: "Nie
jedz czekolady", to znaczy, jest to niemożliwe, nie możecie
tak mówić. Powiedziałam już raz, żebyśmy przestali dawać
dzieciom czekoladę, bo nie mogą jej strawić            z powodu
wątroby, i żebyśmy dawali im w dzieciństwie rzeczy stawne dla
wątroby, dopóki nie dorosną.    I dlaczego nie rozcieńczyć
trochę bardziej tej czekolady? Ale wtedy pytają: "Jak możesz,
Matko? To by było zbyt raptowne, dzieci będą protestować, jak nie
będzie czekolady". Stopniowo się odzwyczają. Zaczynacie mieć
problem z wątrobą. Stajecie się chudzi, mizerni, bladzi,
agresywni, cały czas jesteście niezadowoleni, narzekacie, ciągle
macie o coś pretensje, nigdy nie czujcie radości i nigdy nie
czujecie się szczęśliwi. Mówicie, że to napięcie, stres i tym
podobne rzeczy - ale to nic innego, tylko nadaktywność tej czakry. 

Napięcie jest
spowodowane tym, że cała ta czakra pokryta jest tym balonem, a
balon ciągle rośnie i rośnie i rośnie. On musi się zmniejszyć.
Tylko w Sahaja Yodze możemy naprawdę stawić czoła naszemu ego,
ponieważ tylko poprzez Sahaja Yogę możemy zobaczyć samych siebie.
Ale dla ludzi to jest prawie niemożliwe, żeby stawić stawić czoła
sobie samemu, nawet po Realizacji to jest dla nich trudne.           
     

Na przykład, całe to
nasze utożsamianie się z niewłaściwymi rzeczami. Powiedzmy, że
mam tu do czynienia z Anglikami. Zanim zacznę mówić o niewłaściwym
utożsamianiu się Anglików, muszę potępić wszystkich Hindusów
na świecie, wszystkich Japończyków, Chińczyków, Greków,
wszystkich innych, a potem mogę powiedzieć: "Jest także coś
nie w porządku z Anglikami". Bo inaczej biada Mi! 

Dopóki nie potępię
całej reszty świata, nie mogę zacząć mówić, że my też
błędnie utożsamiamy się, to siedzi w naszych głowach, i jesteśmy
przez to uciskani. Uważamy, że jest to właściwy sposób życia,
przez który dręczyliśmy innych. A cały ten problem z ego jest tak
powszechnie panujący w naszym społeczeństwie. Więc z góry
przepraszam, ale mówię o społeczeństwie zachodnim. Kiedy jestem w
Indiach, mówię do nich, ale teraz mówię do was i uważajcie mnie
również za osobę z Zachodu. Jako osoba z Zachodu widzę pewne
rzeczy. Nie czuję się źle z tego powodu. Mówię o pewnych naszych
tak zwanych tradycjach - dopiero co o tym dzisiaj rozmawiałam. Jak
np. sposób, w jaki sadza się ludzi przy stole, do obiadu. 

No dobrze, taka prosta
rzecz jak siadanie przy stole, do obiadu. Obiad jest na stole,
ponieważ Bóg był dla nas łaskawy; to jest dla naszego dobra, dla
naszej przyjaźni, siadamy wszyscy razem do stołu. Ale kiedy
idziecie na oficjalny obiad albo organizujecie oficjalny obiad,
ludzie będą siedzieć i dyskutować po dziesięć razy, gdzie ma
usiąść X, gdzie ma usiąść Y a gdzie ma usiąść Z. Ale jeżeli
jest najmniejszy błąd w sadzaniu ludzi według daty urodzenia,
koloru twarzy, koloru włosów, wzrostu, nosa, górnej wargi;
jakakolwiek różnica w porównaniu z książką 'Kto jest kim’,
widzicie, że kogoś umieszczono nieco wyżej pod względem
wykształcenia niż was, i wam się dostało. Wszystkie dobre
intencje, żeby ludzi ugościć, zaprzyjaźnić się, stworzyć coś
wspaniałego, to wszystko idzie na marne. Cały wysiłek jest
stracony i myślicie sobie: "Co ja zrobiłem? Sprawdzałem we
wszystkich słownikach i w 'Kto jest kim’". Wiecie, ta książka
'Kto jest kim' to jeszcze inna forma pompowania ego. Ale pomimo tego,
gdzieś zrobiono błąd i ktoś bardzo protestował, że Pan X, który
jest o jeden dzień młodszy od niego, został posadzony przed nim.
To największa katastrofa dla jego ego. 

Widzicie, biedne
krzesła nie wiedzą co się dzieje, mają tylko różne kształty.
Jak my walczymy o takie rzeczy, pompując nasze ego. I tak dalej, i
tak dalej, tyle mamy takich rzeczy. A co jeśli powiem: "Wyrzućcie
wszystkie te bzdury, dlaczego tego nie wyrzucicie? Po co wam te
wszystkie protokoły, to i tamto? Dlaczego ten pan musi mieć to
siedzenie, tamto siedzenie? W końcu, jeśli dziesięć osób
przyjdzie na obiad, mogą sami znaleźć sobie miejsca, nie będzie
bałaganu z dziesięcioma osobami. Nawet z dwudziestoma, nawet z
trzystoma. Dlaczego mają być przypisane miejsca? Jeśli
zaprosiliście trzysta osób, to w porządku, oni wszyscy usiądą.
Jeśli kilka osób pozostanie, usiądą na drugą albo na trzecią
turę. 

Po co w ogóle tak się
śpieszyć z tym programem, albo robić to w taki systematyczny
sposób, kiedy po programie każdy czuje się taki 'niesystematyczny'
zaniepokojony i zdenerwowany? Z całym tym systemem, z tymi
poprawkami i całym naszym wspaniałym planowaniem, w końcu okazuje
się, że staliśmy się wszyscy głupcami. Musi być w tym coś
złego. Widzicie, to zachowanie ludzi, staje się takie gorączkowe i
zaczyna się nagonka. Każdy będzie przychodził z poważną miną:
"Och, jakie złe kierownictwo, wiecie, oni to zrobili". Co
to jest? Nie można nawet zrozumieć, jaką wartość mają te
rzeczy. 

To dlatego musimy być
jak małe dzieci. Dla nich nie ma znaczenia gdzie siedzą. I tacy
jesteśmy dorośli i dojrzali, musimy być także dojrzali; podczas
gdy dzieci ciągle są niewinne i nic nie wiedzą, one mają jeszcze
dorosnąć. My mamy być dorośli - a to, co w nas urosło, to nic
innego, jak tylko Pan Ego a nie wy. 

Kiedy tak się dzieje,
w najgorszym przypadku - wiecie, czytałam w dzisiejszej gazecie o
tym Ripper [Rozpruwaczu]. Teraz doszli do wniosku, że on nie jest
psychopatą, ale jest motywowany przez ego. On robi to wszystko, żeby
zadowolić swoje ego, bo dzwoni na policję, przesyła jak wy to
nazywacie - taśmę, żeby mogli go złapać. Mówi im: "Spotkam
się z wami tutaj", a oni nadal nie mogą go złapać. Więc on
sobie myśli, że jest tak wielki, że nikt nie może go złapać, że
jest największym złodziejem, który się narodził i nikt nie może
go złapać. Więc to jest tak zwany - on nie jest psychopatą, ale
nie wiem jak można nazwać takiego człowieka. Jaką nazwę dać
takiemu egoiście, który kieruje się ego, próbuje zadowolić swoje
ego, a jeśli jego ego nie jest przez coś usatysfakcjonowane, on sam
nie czuje się usatysfakcjonowany. Nie wiem, jak powinniście go
nazwać. A więc, jak przezwyciężyć to ego? 

Ponieważ ludzie
stopniowo zaczynają widzieć: to się dzieje. Zaczynają widzieć
Pana Ego: "O, świetnie, pojawiasz się, poddajesz mi pomysły,
w porządku". Czasami czujecie się bardzo przygnębieni myślą,
że identyfikowaliście się z Panem Ego. W porządku, widzicie swoje
ego. I kiedy widzicie swoje ego, co z nim robicie? Albo kiedy ktoś
powie: "Lepiej uważaj na swoje ego", co się wtedy dzieje?
Zaczynacie z tym walczyć. Mówicie: "Nie, nic nie będę mówić,
będę cicho. Niech sobie mówią, co tylko chcą". Wtedy
stajecie się innym rodzajem człowieka. 

Stajecie się osobą,
która cierpi z powodu superego. Macie w sobie ciągle pragnienia,
ale nie działacie, i moglibyście powiedzieć: "Niech inni mnie
atakują, ja nie będę nikogo atakować. Będę bardzo uważać,
żeby nie atakować". Ale co się wtedy dzieje, to to, że wasza
lewa strona się podnosi - to znaczy lewa strona podnosi się po
prawej stronie, superego i spycha ego do dołu, ale ono z powrotem
odskakuje i robi się taka huśtawka. Przez takie zachowanie, kiedy
walczycie ze swoim ego, ono siedzi wam na głowie jeszcze bardziej.
Im bardziej z nim walczycie, tym mocniej ono siedzi wam na głowie.
Nie wolno wam z nim walczyć. Przypuśćmy, że weźmiecie balon i
zaczniecie w niego uderzać, on wam odda jeszcze mocniej. Im bardziej
uderzacie, tym bardziej balon będzie uderzał. A więc, jak wypuścić
powietrze z ego? 

Jaki jest sposób na
wypuszczenie powietrza z balonu? W taki sam sposób, weźcie szpilkę
i nakłujcie go. Po prostu spójrzcie na siebie, powiedzcie: "Panie
taki a taki, jak się masz?" I śmiejcie się z siebie.
Powiedzcie: "O, byłeś tak bardzo nieszczęśliwy, kiedy
zobaczyłeś gdzie cię posadzili. No dobrze, weź teraz ode mnie tę
szpilę!" I tak właśnie wypuszczacie z niego powietrze, przez
zobaczenie go i przez cały czas naśmiewanie się z niego i
przekłucie go szpilką. Nie przez uciskanie, tak jak ludzie robią
spychając je w dół i zabijając je, ale tylko przez zrobienie w
nim dziurki. W ten sam sposób, zacznijcie patrzeć na siebie i
miejcie z tego ubaw. Tylko ludzie dobrze wiedzą, jak się z siebie
śmiać. 

Żadne zwierzę tego
nie wie. Jak nauczycie śmiać się z samych siebie, wasze ego się
zmniejszy. Bo inaczej ego będzie wam sugerować, że cokolwiek
robicie, jest najlepsze: "To musi być zrobione, tamto być
zrobione. Jestem najlepszy. Wszystko robię dobrze" i możliwe,
że kogoś torturujecie, kogoś atakujecie, robiąc to, co wam ego
każe, jak wariaci. I możecie być prawdziwymi wariatami, ale o tym
nie wiecie. Ponieważ z tym ego człowiek może odnosić tak zwane
sukcesy, stać się wspaniały, błyszczeć, zwyciężać    w
konkursach i nikt się nie odważy mu nic powiedzieć, bo można
oberwać po nosie. On staje również bardzo potężny i mocny, i
każdy pragnie być do niego podobny w zachowaniu itp. Ale przychodzi
czas, kiedy on uświadamia sobie: "Wisiałem w powietrzu.
Zapomniałem o moich podstawach i teraz muszę zejść w dół",
tak jak Humpty Dumpty, który siedział na murku i miał ciężki
upadek. 

Taki piękny…
napisany dawno temu. I wszyscy ludzie… i wszystkie królewskie
konie, i wszyscy ludzie nie mogli złożyć Humpty Dumpty z powrotem
do kupy, widzicie? Więc nie bądźcie Humpty Dumpty i nie żyjcie z
tą ideą o Humpty Dumpty w swojej głowie. Zwalczenie swojego ego,
to bardzo prosta rzecz. I tak się bardzo często dzieje w Sahaja
Yodze, to jest bardzo powszechne w Sahaja Yodze, gdzie wasza własna
Kundalini stwarza takie sytuacje, że jesteście zaszokowani swoim
ego. Nie musicie osądzać innych, patrzcie tylko na siebie, to jest
dla waszego własnego dobra. Jeśli ktoś jest egoistą, to nie robi
wam żadnej różnicy. Albo, jeśli nie jest egoistą, to nie ma
znaczenia. Liczy się tylko to, żebyście sami zobaczyli, co jest
dla was dobre a co złe. Tak to się wypracowuje. 

Bo inaczej, tak jak
zawsze mówię, że kiedy wygłaszam wykład, natychmiast zaczynamy
myśleć o kimś, kogo uważamy za egoistę. A właściwie, Ja to
mówię do każdego z was, byłoby lepiej, żebyście to zobaczyli.
Kiedy to ego pokrywa całą waszą głowę, całą Sahastrarę
pokazaną tutaj, wtedy ten biały obszar zostaje zasłonięty. Wasza
Brahmarandhra, obszar który Kundalini musi przebić, też jest
zasłonięta. To bardzo trudna sytuacja, całość jest zakryta i tu
jest to przejście, ten mały przesmyk. I nie możecie otworzyć
tutaj przejścia. Trzeba otworzyć to przejcie w tym miejscu. A więc
to musi się cofnąć, i to musi przejść przez środek w ten
sposób. Tylko wtedy Kundalini może się wznieść. 

Zatem, co się dzieje
na tym etapie: zobaczycie, że tacy ludzie mają bardzo słabą lewą
rękę i czują mrowienie w prawej ręce. Wtedy podnosicie im lewą
stronę i przekładacie ją na prawą. I ponownie podnosicie im lewą
stronę i przekładacie ją na prawą, i znowu podnosicie im lewą
stronę i przekładacie ją na prawą, w ten sposób obniżycie ego.
A kiedy ego schodzi w dół, wtedy to się podnosi i pojawia się tu
przestrzeń, a wtedy podnosicie środkiem Kundalini. Kundalini się
podniesie i wtedy je zawiązujecie. 

To jest Shri Vidya. To
jest wiedza o Shri, o Duchu Świętym. Wszelka inna wiedza, to, gdzie
macie usiąść według protokołu, zależnie od tego kim jesteście,
to wszystko jest avidya i nonsens. Czytając "Kto jest Kim",
nie wiem, czym się staniecie, potworami albo kimś w stylu
Frankensztajna. To jest vidya, Shri Vidya jest tajemnicą Ducha
Świętego, i przy jej pomocy równoważycie swoje ego i superego.
Wasze pragnienie i akcja powinny być równe; po czym podnosicie
Kundalini i zawiązujecie je. 

W dawnych czasach
ludzie, którzy byli Zrealizowanymi Duszami, wspaniałymi ludźmi,
próbowali zwrócić waszą uwagę na problemy ego, mówili to wam.
Mówili, że nie powinniście mieć ego; ego jest najgorszą rzeczą,
nie powinno się utożsamiać z ego, i tym podobne. Jak tylko to
powiedzieli, ludzie zaczęli szukać jak to zrobić i w wyniku tego,
sami stali się wielkimi "mistrzami" i sami stali się
egoistyczni. 

Faktem jest, że wy
niczego nie robicie. To nie ego macie, lecz macie złudzenie ego. Tak
naprawdę niczego nie robicie - wszystko jest zrobione przez Boga. On
jest Sprawcą, On ma wielkie Ego. Wasze ego to tylko kopia, sztucznie
zrobiona. On tworzy kwiaty a wy tworzycie plastikowe kwiaty i
mówicie: "O stworzyliśmy kwiaty". On robi wszystko. On
jest tym, który wyraża Swoje Ego, podczas gdy wy czujecie, że coś
zrobiliście. To się wam przytrafia, kiedy dostajecie Realizację,
gdy wasza Kundalini przebija Brahmarandhrę, wtedy czujecie siłę,
która przez was przepływa. Kiedy położycie na kimś ręce, wtedy
czujecie, że jego Kundalini się podnosi, i okazuje się, że
czujecie centra tej osoby i czujecie, na których czakrach ma
blokady, i jesteście zdumieni. Mówicie: "Matko, jak to
możliwe? Przed chwilą nic nie czułem, co się nagle ze mną
stało?" 

Ponieważ wasze ego
opadło i zjednoczyło się z Ego Boga, staliście się kanałem dla
Boskiej Siły. Spełniacie rolę instrumentu, kogoś, kto jest pusty
w środku, i przechodzi przez was Siła Boga, która robi prawdziwą
pracę, podczas gdy wy myślicie: "Ja to robię" - tak jest
przed Realizacją. Ale po Realizacji po prostu czujecie: "O to
płynie przeze mnie. Widzę, że płynie. Widzę, że to działa, to
jest". 

Zaczynacie mówić w
trzeciej osobie, nie mówicie "ja". Tylko mówicie: "To
idzie, to nadchodzi, tego nie ma. To się blokuje, tamto się
blokuje". Mówicie w trzeciej osobie. Nie mówicie: "Ja się
blokuję". Nie mówicie też: "Podniosłem Kundalini",
mówicie: "Kundalini się podniosła". Kto Ją podnosi?
Nigdy nie mówicie, że podnieśliście Kundalini. Wy po prostu
mówicie: "Kundalini się podniosła. Nie podnosi się. Po
prostu tam idzie", tak jakbyście byli instrumentem jakiejś
siły, która przez was płynie. 

I tak właśnie się z
tym jednoczymy i wszystkie nasze karmy rozpuszczają się, i stają
się akarmą. Karma jest tylko efektem fałszywych pomysłów naszego
ego. Kiedy nie ma ego, gdzie będą karmy? Kiedy nie ma tego, który
wszystko robi, wykonawca jest skończony, kto wtedy będzie robił
karmy, kto będzie brał na siebie ciężar karmy? Widzicie, to jest
tak, jakby była organizacja, i ona mówi: "Mamy takie aktywa i
takie długi, taki jest nasz stan". Ale kiedy ta organizacja,
czy ten związek znika, wtedy na kim ciąży dług? W ten sam sposób,
wy jako wasze ego znikacie i nie ma już efektów karmy - wszystko
jest skończone. To było kiedy Pan Ego tam był. Kiedy nie ma Pana
Ego, razem z nim znika cały ciężar odpowiedzialności. 

Staliśmy się teraz
czymś innym. Staliśmy się samym sobą, Atmą [Duszą]. A kiedy
stajemy się Atmą, jesteśmy zjednoczeni z Uniwersalną Jaźnią, i
Ona sprawia, że robimy to wszystko. Tak jak moje palce nie mają
ego, że coś nimi robię i dotykam mojego kciuka, bo to jest część
tego ciała. Nasz nos nie ma ego, bo robi coś specjalnego - czyli
oddychanie. Mówimy "oddycham", nie mówimy "ten nos
oddycha". Ani nos tak nie mówi ani te usta, którymi jemy
jedzenie, one nigdy nie mówią: "Ja jem z powodu ust".
Kiedy jednoczycie się z całością, wtedy po prostu czujecie, że
całość pracuje, a my, jako jej część, pracujemy z nią; a całe
poczucie odpowiedzialności się skończyło. 

Dzisiaj jest mały
wykład o ego, ponieważ czułam, że powinnam wam powiedzieć, że
jeśli chodzi o obudzenie Kundalini na Zachodzie, to jak dotąd, cały
czas mieliśmy ten problem z ego. Również z superego; ci, którzy
chodzili do fałszywych guru byli opętani przez swoich guru. 

Bo w społeczeństwie
zorientowanym na ego, to jest odbierane z ulgą. Bo koń galopował i
wy musieliście siedzieć na koniu. I ktoś inny przychodzi i mówi:
"No dobrze, przywiążę ciebie do brzucha konia a sam usiądę
na koniu". To jest właśnie to opętanie. Więc wy czujecie
ulgę, oddychacie z ulgą: bo w końcu nie musicie już dosiadać
konia, nie musicie przeskakiwać przeszkód. A ktoś inny jest teraz
jeźdźcem, czujecie, że to nie wasza odpowiedzialność, że teraz
jeździec to robi. Ale po jakimś czasie spędzonym pod brzuchem
konia, zaczynacie mieć różnego rodzaju problemy fizyczne. Bo przy
superego macie fizyczne problemy. To dlatego lepiej jest mieć
superego niż mieć ego, ponieważ nie chcecie już więcej mieć
fizycznych problemów. 

Ale kiedy macie ego,
nigdy nie macie fizycznych problemów, wasza fizjologia może się
polepszyć; czujecie się bardzo dobrze, na szczycie świata,
widzicie. W tym jest największy problem. Ale macie umysłowe
problemy: nie możecie spać, nie przestajecie myśleć, ciągle
myślicie jak wariaci, nie możecie przestać myśleć. Ciągle się
spieracie, że to musi być zrobione. Myślicie, że każdy powinien
robić waszą pracę, podczas gdy wy jesteście najważniejszą
osobą, która się narodziła. Więc pozostał nam tylko jeden
wybór, żeby być w centrum, obserwować swoje ego i superego.
Wszyscy Sahaja yogini, którzy są zrealizowani, muszą obserwować
siebie, nie innych. Nie powinniście oczekiwać niczego od innych, a
dla odmiany oczekujcie tylko od samych sobie. Bo przy ego, to właśnie
robicie i oczekujecie czegoś od innych, przy superego robicie rzeczy
dla innych. Ale w Samorealizacji emitujecie swojego Ducha jako
radość, miłość i piękno. 

Niech was wszystkich
Bóg błogosławi. 

Czy mamy zacząć
dawanie wibracji i Realizację? Więc prosiłabym, żeby nowi ludzie
zdjęli skarpetki, tak będzie jeszcze lepiej, będzie im wygodniej.
Połóżcie obie ręce na ziemi i siądźcie wygodnie. Chyba już
rozumiecie, że to jest wydarzenie. Nie możecie tego wymusić, nie
możecie tego wypracować, to musi się wam przydarzyć. A więc w
ogóle nie trzeba się wysilać. To samo działa, zaufajcie temu.
Połóżcie ręce w ten sposób, bardzo wygodnie. Nawet dla Sahaja
yoginów, to jest bardzo ważne, żeby ciągle wystawiali się na
działanie Boskiej Mocy. I wtedy, kiedy Kundalini przebija
ciemiączko, natychmiast zaczynacie czuć chłodne wibracje na
dłoniach. 

Ale to dzieje się na
bardzo różnych poziomach u wielu ludzi. Na przykład, niektórzy
ludzie mogą nawet nie doświadczyć podniesienia Kundalini - tylko
niektórzy, bardzo niewielu. U niektórych Kundalini podnosi się do
połowy i ponownie opada. U niektórych zatrzymuje się na różnych
czakrach, w tych ośrodkach, które nie są w porządku. Niektórzy
ludzie mogą mieć jakiś fizyczny problem i mogą czuć, jak
wychodzi z nich trochę gorąca. Szczególnie, jeśli macie problem z
wątrobą, dużo gorąca tam się zbiera i to gorąco wychodzi z
ciała. 

A niektórym ludziom
robi się bardzo zimno. Obie rzeczy nie są w porządku, powinno się
być pośrodku. Jeśli zaczynacie odczuwać chłód na dłoniach to
jest absolutnie w porządku. 

Ale powiedzmy, że
czujecie gorąco na dłoni, to nie szkodzi, możecie to po prostu
odrzucić i to się wypracuje. 

Jeśli powieki wam
drgają przy zamkniętych oczach, wtedy proszę otwórzcie oczy.
Kundalini podnosi się    u wszystkich, to działa u wszystkich;
tylko różne są reakcje w osobie, która tego doświadcza. Jeśli
nie ma reakcji, albo jeśli Kundalini się nie podnosi, nie ma co źle
się czuć czy dyskutować na ten temat. Jeśli mogłabym to zrobić
poprzez dyskusje, wzięłabym was do sądu, żeby się spierać. Ale
widzicie, nie mogę tego zrobić poprzez argumenty - to musi się w
was wydarzyć. Pragnę, żeby to się wydarzyło, tak samo wy
powinniście pragnąć, żeby wam się to przydarzyło. To nie
podlega dyskusji, widzicie, to się dzieje. Jak można dyskutować na
temat wykiełkowania nasionka? Jeśli nie wykiełkuje, nie możecie
się z nim spierać, prawda? Musi samo wykiełkować. 

To jest takie proste.
Jeśli nie wykiełkuje, musi coś być nie w porządku, prawda? Potem
ci ludzie, którzy są zrealizowani i o tym wiedzą, ci, którzy mają
Shri Vidya [Świętą Wiedzę] i wiedzą jak to robić, oni podnoszą
wasze Kundalini, zawiązują je i tak dalej. Ale nawet wtedy, ludzie
mają obiekcje. Nie mają nic przeciwko rozbieraniu się do naga i
tańczeniu, i robieniu różnych bezsensownych rzeczy, ale jeśli
ktoś podnosi Kundalini i zawiązuje je, to im się nie podoba. Nie
ma co się bać Kundalini. Ktoś mi powiedział, że Anglicy bardzo
się boją słowa 'Kundalini'. Myślę, że niektórzy Anglicy,
którzy pojechali do Indii, próbowali dojść do tego tylnymi
drzwiami i spotykali wszelkiego rodzaju okropnych ludzi, którzy nie
mieli żadnego prawa mówić o Kundalini, którzy nie byli świętymi
ludźmi, to byli niewuprawnieni ludzie: oczywiście, że wtedy ludzie
mieli bardzo złe doświadczenia z Kundalini. To jest tak, jakby
jakiś wieśniak przyszedł i włożył palce do gniazdka
elektrycznego i powiedział, że elektryczność jest okropnym
doświadczeniem. W taki sam sposób to mogło przydarzyć się wielu
ludziom, ale nie ma się czego bać. 

To bardzo dobrze
działa i daje wam to, o co prosicie. Ona [Kundalini] jest waszą
Matką, waszą Wieczną Matką. Ona kocha was bardziej niż
ktokolwiek może kochać. Ona nie będzie wam sprawiać żadnego
kłopotu. Ona po prostu czeka, żeby dać wam ponowne narodzenie.
Więc już nie ma się czego bać. Również co do guru, nie
powinniście się ze Mną kłócić. Jeśli wasz guru jest w
porządku, sama napiszę do niego miły list. Ale jeśli on nie jest
w porządku, jeśli zrujnował wasze Kundalini, waszą Agnyę, wasze
czakry, wtedy muszę wam powiedzieć, że coś jest z tym facetem nie
w porządku, bo sami tego nie wiecie. Powiedzmy, że policjant
przychodzi do waszego domu i mówi, że był w waszym domu włamywacz.

Czy jesteście źli na
policjanta? Tak samo nie powinniście być źli na Mnie, ponieważ
jeśli wasz guru jest prawdziwym guru, satguru, uwielbiałabym tego
człowieka, byłabym szczęśliwa; ale jeszcze się z tym nie
spotkałam - jest ich bardzo niewielu, bardzo, bardzo niewielu. A
więc po co utożsamiać się ze swoim guru? On nigdy nie dał wam
żadnych mocy, popisywał się tylko swoimi mocami, i obiecywał wam,
że dzisiaj staniecie się żabą, jutro staniecie się ptakiem a
trzeciego dnia staniecie się rybą, a potem amebą. Przypuszczam, że
skończycie jako ameba! [komentarz yoginki] Albo ona tak mówi, inna
rzecz, którą wam powiedzą: jeśli nic wam nie dają, to zabierają
wam wszystkie pieniądze i mówią, że musicie się odwiązać od
pieniędzy, od rodziny, "przyślij mi swoją żonę, ja się nią
zaopiekuję a ty się zdystansuj. Daj mi wszystkie swoje pieniądze,
wszystko" - tak ona mówi. I wtedy mówicie: "Dałem ci
wszystko i ciągle nic nie dostałem". Wtedy oni powiedzą:
“Lepiej jedźcie w Himalaje. Oto jest przyczyna, musicie pojechać 
               w Himalaje. 

Dajcie mi swój piękny
dom, będę tu sobie mieszkał. Dajcie mi wszystkie pieniądze, które
macie, i pojedźcie w Himalaje. Lepiej będzie, jeśli nie będziecie
żonaci, lepiej się rozwiedźcie. Widzicie, ona tylko stoi wam na
drodze". I wysyła was w Himalaje, żebyście tam umarli z
zimna. [dalszy komentarz yoginki] Ona mówi: "Gdzie jest osoba
nieskazitelna? … Mówi: "Pomiędzy dziesiątkami tysięcy, Ty
jesteś jedyna". Nie - nawet jeśli oni nie są czyści, nie
powinni być tak złowrodzy. Wy jesteście o wiele lepszymi ludźmi
niż oni, bo nie jesteście zrealizowani i nie propagujecie niczego,
nie wygłaszacie kazań. Oni nie są zrealizowani i nie mają żadnego
upoważnienia. Wywiesili swoje flagi i zbierają pieniądze.
Zbieracze pieniędzy. 

No dobrze, jeszcze raz
wyciągnijcie ręce w moim kierunku. Zdejmijcie skarpetki. Teraz
dobrze. Postawcie tak swoje stopy, połóżcie tak ręce. Zamknijcie
oczy, proszę zamknijcie oczy. Teraz, pierwsza rzecz, która się
wydarza to to, że jeśli obserwujecie swoje myśli, to nie ma myśli.
To oznacza, że przekroczyliście Agnyę. 

